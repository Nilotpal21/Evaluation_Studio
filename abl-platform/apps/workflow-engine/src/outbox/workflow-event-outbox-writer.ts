/**
 * Workflow event outbox writer (LLD §3.1).
 *
 * Responsibilities
 * ----------------
 * 1. `buildOutboxPayload` — pure function that accepts a validated workflow
 *    or human-task event and returns the Mongoose-shape outbox document.
 *    The outbox row's `_id` equals the event's `event_id` (UUIDv7) — this
 *    is the dedup contract the Kafka consumer relies on (see HLD §5.3).
 *
 * 2. `WorkflowEventOutboxWriter` — thin persistence wrapper around
 *    `WorkflowEventOutboxModel.insertMany`. Accepts a nullable
 *    `ClientSession`. When the caller's `withTransaction` evaluates
 *    `canUseTransactions()` to `false` (standalone Mongo), the session is
 *    `null` and we fall back to a non-transactional insert — production
 *    runs on replica sets so this path exists only to keep local dev
 *    without `rs.initiate()` from crashing.
 *
 * This module never opens a transaction itself. The atomicity guarantee
 * (domain write + outbox write commit together) belongs to the caller
 * (`withTransaction` in `execution-store-with-outbox.ts` /
 * `human-task-store-with-outbox.ts`).
 */

import type { ClientSession } from 'mongoose';
import { createLogger } from '@abl/compiler/platform';
import type { WorkflowExecutionEvent, HumanTaskEvent } from '@abl/eventstore/schema';

export const WORKFLOW_EXECUTION_TOPIC = 'abl.workflow.execution';
export const HUMAN_TASK_TOPIC = 'abl.human.task';

const log = createLogger('workflow-engine:outbox-writer');

/**
 * Entity kinds carried on each outbox row. Used by the poller to route to
 * the right Kafka topic and by the cascade hook to identify deletion scope.
 */
export type OutboxEntityKind = 'workflow_execution' | 'human_task';

/**
 * Outbox document shape — mirrors `WorkflowEventOutboxModel` exactly.
 * Kept in this module so the writer doesn't need a mongoose runtime import
 * for its interface (important for isolated unit tests).
 */
export interface WorkflowEventOutboxDoc {
  _id: string;
  tenantId: string;
  projectId: string;
  entityKind: OutboxEntityKind;
  entityId: string;
  topic: string;
  eventType: string;
  eventVersion: string;
  occurredAt: Date;
  payload: Record<string, unknown>;
  publishedAt: Date | null;
  lastError: string | null;
  retryCount: number;
  expiresAt: Date | null;
}

/**
 * Minimal insertMany interface — decoupled from Mongoose concrete types
 * so unit tests can inject a plain fake without pulling in the mongoose
 * runtime (much faster).
 */
export interface OutboxModelLike {
  insertMany(
    docs: WorkflowEventOutboxDoc[],
    options?: { session?: ClientSession },
  ): Promise<unknown>;
}

/**
 * Inputs to `buildOutboxPayload` — an entity kind + a validated event.
 * Split by discriminated union so TypeScript narrows `event` correctly
 * and we can't accidentally attach a workflow event to the human-task
 * topic or vice versa.
 */
export type BuildOutboxInput =
  | { entityKind: 'workflow_execution'; event: WorkflowExecutionEvent }
  | { entityKind: 'human_task'; event: HumanTaskEvent };

/**
 * Build an outbox document from a validated event.
 *
 * The event is treated as the source of truth: `event_id`, `tenant_id`,
 * `project_id`, and `occurred_at` are snapshotted into top-level columns
 * for indexing; the full event becomes `payload` so the poller can forward
 * it verbatim to Kafka without re-shaping.
 *
 * Preconditions (checked by TypeScript + Zod upstream):
 *  - `event.event_id` is a valid UUIDv7 (generated by the emitter).
 *  - `event.event_type` is an enum member — topic routing below relies on it.
 *
 * The function is intentionally synchronous and side-effect free — unit
 * tests exercise it directly without any Mongo or Kafka infrastructure.
 */
export function buildOutboxPayload(input: BuildOutboxInput): WorkflowEventOutboxDoc {
  const { entityKind, event } = input;
  const occurredAt = new Date(event.occurred_at);
  if (Number.isNaN(occurredAt.getTime())) {
    throw new Error(
      `buildOutboxPayload: event.occurred_at is not a valid ISO-8601 datetime: ${event.occurred_at}`,
    );
  }

  const topic = entityKind === 'workflow_execution' ? WORKFLOW_EXECUTION_TOPIC : HUMAN_TASK_TOPIC;
  const entityId =
    entityKind === 'workflow_execution'
      ? (event as WorkflowExecutionEvent).execution_id
      : (event as HumanTaskEvent).task_id;

  return {
    _id: event.event_id,
    tenantId: event.tenant_id,
    projectId: event.project_id,
    entityKind,
    entityId,
    topic,
    eventType: event.event_type,
    eventVersion: event.event_version ?? '1.0.0',
    occurredAt,
    payload: event as unknown as Record<string, unknown>,
    publishedAt: null,
    lastError: null,
    retryCount: 0,
    expiresAt: null,
  };
}

/**
 * Writer that persists outbox rows. Intentionally does not own the
 * transaction — the caller passes a `ClientSession` (or `null`) from
 * `withTransaction`.
 */
export class WorkflowEventOutboxWriter {
  constructor(private readonly model: OutboxModelLike) {}

  async writeWithSession(
    docs: WorkflowEventOutboxDoc[],
    session: ClientSession | null,
  ): Promise<void> {
    if (docs.length === 0) return;

    await this.model.insertMany(docs, session ? { session } : undefined);

    // Structured log per row — trace event inventory (LLD §3.7 +
    // LLD-E-3). Rendered as workflow.outbox.enqueued by the logger.
    for (const doc of docs) {
      log.info('workflow.outbox.enqueued', {
        event_type: 'workflow.outbox.enqueued',
        event_id: doc._id,
        entity_kind: doc.entityKind,
        entity_id: doc.entityId,
        topic: doc.topic,
        tenant_id: doc.tenantId,
        project_id: doc.projectId,
      });
    }
  }
}
