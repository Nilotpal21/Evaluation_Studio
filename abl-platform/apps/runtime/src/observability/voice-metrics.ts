/**
 * Voice Observability
 *
 * Production-grade observability for voice pipeline:
 * - OTEL spans for distributed tracing (STT → LLM → TTS)
 * - Metrics for dashboards and alerting
 * - Event emission for TraceStore integration
 *
 * Span hierarchy:
 *   voice_turn (parent)
 *   ├── stt (Deepgram transcription)
 *   ├── llm (Agent processing)
 *   └── tts (ElevenLabs synthesis)
 */

import { trace, context, SpanStatusCode, type Span, type Context } from '@opentelemetry/api';
import { metrics, type Meter } from '@opentelemetry/api';

// =============================================================================
// TRACER & METER
// =============================================================================

const tracer = trace.getTracer('voice-pipeline', '1.0.0');
const meter: Meter = metrics.getMeter('voice-pipeline', '1.0.0');

// =============================================================================
// METRICS
// =============================================================================

// Voice turn metrics
const voiceTurnDuration = meter.createHistogram('voice.turn.duration', {
  description: 'Total duration of a voice turn (user speaks → agent responds)',
  unit: 'ms',
});

const voiceTurnCount = meter.createCounter('voice.turn.count', {
  description: 'Number of voice turns processed',
});

// STT metrics
const sttDuration = meter.createHistogram('voice.stt.duration', {
  description: 'Duration of speech-to-text transcription',
  unit: 'ms',
});

const sttConfidence = meter.createHistogram('voice.stt.confidence', {
  description: 'Confidence score from STT',
  unit: '1',
});

// LLM metrics (voice-specific, separate from general LLM metrics)
const voiceLlmDuration = meter.createHistogram('voice.llm.duration', {
  description: 'Duration of LLM processing for voice turn',
  unit: 'ms',
});

// TTS metrics
const ttsDuration = meter.createHistogram('voice.tts.duration', {
  description: 'Total duration of text-to-speech synthesis',
  unit: 'ms',
});

const ttsFirstChunkLatency = meter.createHistogram('voice.tts.first_chunk_latency', {
  description: 'Time to first audio chunk from TTS',
  unit: 'ms',
});

const ttsBytesGenerated = meter.createCounter('voice.tts.bytes', {
  description: 'Total audio bytes generated by TTS',
});

// Silence detection
const silenceThresholdHits = meter.createCounter('voice.silence.threshold_hits', {
  description: 'Number of times silence threshold triggered processing',
});

// =============================================================================
// REALTIME VOICE METRICS
// =============================================================================

const realtimeTurnLatency = meter.createHistogram('voice.realtime.turn_latency', {
  description: 'Latency from user speech end to first audio output in realtime mode',
  unit: 'ms',
});

const realtimeSessionDuration = meter.createHistogram('voice.realtime.session_duration', {
  description: 'Total duration of a realtime voice session',
  unit: 'ms',
});

const realtimeToolCallDuration = meter.createHistogram('voice.realtime.tool_call_duration', {
  description: 'Duration of tool calls within realtime voice turns',
  unit: 'ms',
});

const realtimeInterruptions = meter.createCounter('voice.realtime.interruptions', {
  description: 'Number of barge-in interruptions in realtime voice sessions',
});

const realtimeActiveSessions = meter.createUpDownCounter('voice.realtime.active_sessions', {
  description: 'Number of currently active realtime voice sessions',
});

// =============================================================================
// REALTIME METRIC RECORDING FUNCTIONS
// =============================================================================

export function recordRealtimeTurnComplete(
  sessionId: string,
  turnLatencyMs: number,
  toolCallDurationMs: number,
): void {
  realtimeTurnLatency.record(turnLatencyMs);
  if (toolCallDurationMs > 0) {
    realtimeToolCallDuration.record(toolCallDurationMs);
  }
}

export function recordRealtimeSessionStart(sessionId: string): void {
  realtimeActiveSessions.add(1);
}

export function recordRealtimeSessionEnd(sessionId: string, durationMs: number): void {
  realtimeActiveSessions.add(-1);
  realtimeSessionDuration.record(durationMs);
}

export function recordRealtimeInterruption(sessionId: string): void {
  realtimeInterruptions.add(1);
}

// =============================================================================
// SPAN TYPES
// =============================================================================

export interface VoiceTurnSpan {
  span: Span;
  context: Context;
  sessionId: string;
  startTime: number;
}

export interface VoiceTimingData {
  sessionId: string;
  turnId: string;

  // Timestamps (epoch ms)
  utteranceReceivedAt: number;
  sttStartAt?: number;
  sttEndAt?: number;
  llmStartAt?: number;
  llmEndAt?: number;
  ttsStartAt?: number;
  ttsFirstChunkAt?: number;
  ttsEndAt?: number;

  // Durations (calculated)
  totalDuration?: number;
  sttDuration?: number;
  llmDuration?: number;
  ttsDuration?: number;
  ttsFirstChunkLatency?: number;

  // Data
  utterance?: string;
  response?: string;
  sttConfidence?: number;
  ttsBytes?: number;

  // Status
  success: boolean;
  error?: string;
}

// =============================================================================
// VOICE TURN TRACKING
// =============================================================================

/**
 * Start tracking a voice turn.
 * Call this when silence is detected and processing begins.
 */
export function startVoiceTurn(sessionId: string, utterance: string): VoiceTurnSpan {
  const span = tracer.startSpan('voice_turn', {
    attributes: {
      'session.id': sessionId,
      'voice.utterance': utterance.substring(0, 200), // Truncate for safety
      'voice.utterance_length': utterance.length,
    },
  });

  silenceThresholdHits.add(1);

  return {
    span,
    context: trace.setSpan(context.active(), span),
    sessionId,
    startTime: Date.now(),
  };
}

/**
 * Record STT phase timing.
 * Call this when transcription is finalized.
 */
export function recordSTT(
  parent: VoiceTurnSpan,
  opts: {
    durationMs: number;
    confidence: number;
    transcript: string;
  },
): void {
  const sttSpan = tracer.startSpan(
    'stt',
    {
      attributes: {
        'stt.provider': 'deepgram',
        'stt.duration_ms': opts.durationMs,
        'stt.confidence': opts.confidence,
        'stt.transcript_length': opts.transcript.length,
      },
    },
    parent.context,
  );
  sttSpan.end();

  // Record metrics
  sttDuration.record(opts.durationMs, {
    'stt.provider': 'deepgram',
  });
  sttConfidence.record(opts.confidence);
}

/**
 * Record LLM phase timing.
 * Call this after agent processing completes.
 */
export function recordLLM(
  parent: VoiceTurnSpan,
  opts: {
    durationMs: number;
    responseLength: number;
    model?: string;
  },
): void {
  const llmSpan = tracer.startSpan(
    'llm',
    {
      attributes: {
        'llm.duration_ms': opts.durationMs,
        'llm.response_length': opts.responseLength,
        'llm.model': opts.model ?? 'unknown',
      },
    },
    parent.context,
  );
  llmSpan.end();

  // Record metrics
  voiceLlmDuration.record(opts.durationMs, {
    'llm.model': opts.model ?? 'unknown',
  });
}

/**
 * Start TTS span (call when synthesis begins).
 * Returns a span that should be ended when TTS completes.
 */
export function startTTS(parent: VoiceTurnSpan): Span {
  return tracer.startSpan(
    'tts',
    {
      attributes: {
        'tts.provider': 'elevenlabs',
      },
    },
    parent.context,
  );
}

/**
 * Record TTS first chunk (call when first audio chunk arrives).
 */
export function recordTTSFirstChunk(parent: VoiceTurnSpan, ttsSpan: Span, latencyMs: number): void {
  ttsSpan.setAttribute('tts.first_chunk_latency_ms', latencyMs);

  ttsFirstChunkLatency.record(latencyMs, {
    'tts.provider': 'elevenlabs',
  });
}

/**
 * Record TTS completion.
 */
export function recordTTSComplete(
  parent: VoiceTurnSpan,
  ttsSpan: Span,
  opts: {
    durationMs: number;
    totalBytes: number;
    textLength: number;
  },
): void {
  ttsSpan.setAttributes({
    'tts.duration_ms': opts.durationMs,
    'tts.total_bytes': opts.totalBytes,
    'tts.text_length': opts.textLength,
  });
  ttsSpan.end();

  // Record metrics
  ttsDuration.record(opts.durationMs, {
    'tts.provider': 'elevenlabs',
  });
  ttsBytesGenerated.add(opts.totalBytes);
}

/**
 * End the voice turn span with success.
 */
export function endVoiceTurnSuccess(
  turn: VoiceTurnSpan,
  opts: {
    totalDurationMs: number;
    responseLength: number;
  },
): void {
  turn.span.setAttributes({
    'voice.total_duration_ms': opts.totalDurationMs,
    'voice.response_length': opts.responseLength,
    'voice.success': true,
  });
  turn.span.setStatus({ code: SpanStatusCode.OK });
  turn.span.end();

  // Record metrics
  voiceTurnDuration.record(opts.totalDurationMs, {
    'voice.success': true,
  });
  voiceTurnCount.add(1, {
    'voice.success': true,
  });
}

/**
 * End the voice turn span with error.
 */
export function endVoiceTurnError(turn: VoiceTurnSpan, error: Error | string): void {
  const errorMessage = error instanceof Error ? error.message : error;

  turn.span.setAttributes({
    'voice.success': false,
    'voice.error': errorMessage,
  });
  turn.span.setStatus({
    code: SpanStatusCode.ERROR,
    message: errorMessage,
  });
  turn.span.end();

  // Record metrics
  voiceTurnCount.add(1, {
    'voice.success': false,
  });
}

// =============================================================================
// TIMING DATA HELPERS
// =============================================================================

/**
 * Create a complete timing report from a voice turn.
 * Use this to emit trace events or store for analysis.
 */
export function createTimingReport(data: VoiceTimingData): Record<string, unknown> {
  return {
    type: 'voice_turn_timing',
    sessionId: data.sessionId,
    turnId: data.turnId,
    timestamps: {
      utteranceReceived: data.utteranceReceivedAt,
      sttStart: data.sttStartAt,
      sttEnd: data.sttEndAt,
      llmStart: data.llmStartAt,
      llmEnd: data.llmEndAt,
      ttsStart: data.ttsStartAt,
      ttsFirstChunk: data.ttsFirstChunkAt,
      ttsEnd: data.ttsEndAt,
    },
    durations: {
      total: data.totalDuration,
      stt: data.sttDuration,
      llm: data.llmDuration,
      tts: data.ttsDuration,
      ttsFirstChunk: data.ttsFirstChunkLatency,
      overhead:
        data.totalDuration && data.sttDuration && data.llmDuration && data.ttsDuration
          ? data.totalDuration - data.sttDuration - data.llmDuration - data.ttsDuration
          : undefined,
    },
    data: {
      utterance: data.utterance,
      response: data.response,
      sttConfidence: data.sttConfidence,
      ttsBytes: data.ttsBytes,
    },
    success: data.success,
    error: data.error,
  };
}
