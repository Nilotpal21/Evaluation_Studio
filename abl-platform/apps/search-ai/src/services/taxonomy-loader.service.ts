/**
 * Taxonomy Loader Service
 *
 * Loads and parses domain definitions + organization profiles to create
 * a unified taxonomy for knowledge graph enrichment.
 *
 * Workflow:
 * 1. Load domain definition files (pre-defined JSON in system)
 * 2. Load organization profile (customer-provided markdown)
 * 3. Parse organization profile with LLM (extract structured data from markdown)
 * 4. Merge domain definitions with organization context
 * 5. Validate taxonomy structure
 * 6. Store in MongoDB (knowledge_graph_taxonomy collection)
 *
 * Uses LLM for:
 * - Parsing customer organization profile markdown (flexible structure)
 * - Extracting product names and organization-specific aliases
 * - Extracting disambiguation keywords from product descriptions
 * - Extracting attribute context (typical ranges, aliases)
 */

