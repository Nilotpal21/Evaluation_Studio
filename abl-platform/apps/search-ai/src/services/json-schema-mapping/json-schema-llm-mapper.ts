/**
 * JSON Schema LLM Mapper
 *
 * Analyzes JSON record fields using LLM to determine:
 * 1. Which fields are important for filtering/aggregation
 * 2. How to map them to canonical slots (custom_string_1-20, custom_number_1-10)
 * 3. What vocabulary entries to generate for query-time resolution
 *
 * Called once per JSON file upload — not per record. The resulting mapping
 * is applied to all records in the file.
 *
 * Graceful degradation: if LLM is unavailable, returns empty mapping
 * and chunks are created without canonical metadata (current behavior).
 */

import type { WorkerLLMClient } from '@agent-platform/llm';
import { createLogger } from '@abl/compiler/platform';

const logger = createLogger('json-schema-llm-mapper');

// ─── Types ──────────────────────────────────────────────────────────────

export interface FieldMapping {
  /** Original field name in JSON record */
  sourceField: string;
  /** Canonical slot in OpenSearch (e.g., "custom_string_1", "title", "category") */
  canonicalField: string;
  /** Data type for the canonical field */
  type: 'keyword' | 'number' | 'text' | 'date';
  /** Whether this field should be filterable at query time */
  filterable: boolean;
  /** Whether this field should be sortable */
  sortable: boolean;
  /** Whether this field should be aggregatable */
  aggregatable: boolean;
  /** Human-friendly name for this field (used as the vocabulary term) */
  alias: string;
  /** Synonyms / alternative names users might use for this field */
  synonyms: string[];
  /** Description for LLM context at query time */
  description: string;
  /** Sample values seen in the data (for enum generation) */
  sampleValues?: string[];
}

export interface JsonSchemaMappingResult {
  /** Field mappings from JSON keys to canonical slots */
  mappings: FieldMapping[];
  /** Fields the LLM decided to skip (with reasons) */
  skippedFields: Array<{ field: string; reason: string }>;
  /** Whether the mapping was generated by LLM or is empty (fallback) */
  generatedByLlm: boolean;
}

// ─── Constants ──────────────────────────────────────────────────────────

/** Core canonical fields that JSON fields can map to directly */
const CORE_FIELD_SLOTS: Record<string, { type: string; storageField: string }> = {
  title: { type: 'text', storageField: 'title' },
  description: { type: 'text', storageField: 'description' },
  content_summary: { type: 'text', storageField: 'content_summary' },
  category: { type: 'keyword', storageField: 'category' },
  tags: { type: 'keyword', storageField: 'tags' },
  author: { type: 'keyword', storageField: 'author' },
  status: { type: 'keyword', storageField: 'status' },
};

/** Maximum custom slots available in OpenSearch mapping */
const MAX_CUSTOM_STRINGS = 20;
const MAX_CUSTOM_NUMBERS = 10;
const MAX_CUSTOM_DATES = 5;

/** Maximum sample records to send to LLM */
const MAX_SAMPLE_RECORDS = 5;

/** LLM call timeout */
const LLM_TIMEOUT_MS = 60_000;

// ─── Service ────────────────────────────────────────────────────────────

/**
 * Analyze JSON records and produce a field-to-canonical mapping via LLM.
 *
 * @param sampleRecords - 3-5 representative records from the uploaded JSON
 * @param llmClient - Tenant-scoped LLM client
 * @returns Mapping result with field assignments and vocabulary data
 */
export async function analyzeJsonSchemaWithLlm(
  sampleRecords: Record<string, unknown>[],
  llmClient: WorkerLLMClient,
): Promise<JsonSchemaMappingResult> {
  try {
    // Take at most MAX_SAMPLE_RECORDS
    const samples = sampleRecords.slice(0, MAX_SAMPLE_RECORDS);

    if (samples.length === 0) {
      logger.warn('No sample records provided');
      return { mappings: [], skippedFields: [], generatedByLlm: false };
    }

    // Build a compact schema summary from sample records
    const schemaSummary = buildSchemaSummary(samples);

    // Build LLM prompt
    const systemPrompt = buildSystemPrompt(schemaSummary);
    const userMessage = `Analyze this JSON schema and map the important fields.\n\nSchema summary:\n${JSON.stringify(schemaSummary, null, 2)}`;

    // Call LLM
    const llmResponse = await Promise.race([
      llmClient.chat(systemPrompt, [{ role: 'user', content: userMessage }], {
        maxTokens: 2000,
      }),
      new Promise<never>((_, reject) =>
        setTimeout(() => reject(new Error('LLM timeout')), LLM_TIMEOUT_MS),
      ),
    ]);

    // Parse response
    const result = parseLlmResponse(llmResponse);

    logger.info('JSON schema LLM mapping complete', {
      mappedFields: result.mappings.length,
      skippedFields: result.skippedFields.length,
    });

    return result;
  } catch (error) {
    logger.error('JSON schema LLM mapping failed, falling back to empty mapping', {
      error: error instanceof Error ? error.message : String(error),
    });
    return { mappings: [], skippedFields: [], generatedByLlm: false };
  }
}

// ─── Schema Summary ─────────────────────────────────────────────────────

interface FieldSummary {
  name: string;
  type: 'string' | 'number' | 'boolean' | 'array' | 'object' | 'null';
  /** For arrays: the type of array elements */
  arrayElementType?: string;
  /** Sample values (up to 5 unique) */
  sampleValues: string[];
  /** Approximate number of unique values across samples */
  uniqueCount: number;
  /** Whether the value is typically a long text (>100 chars) */
  isLongText: boolean;
}

interface SchemaSummary {
  fieldCount: number;
  sampleCount: number;
  fields: FieldSummary[];
}

function buildSchemaSummary(samples: Record<string, unknown>[]): SchemaSummary {
  const fieldMap = new Map<string, { values: Set<string>; types: Set<string>; maxLen: number }>();

  for (const record of samples) {
    for (const [key, value] of Object.entries(record)) {
      if (!fieldMap.has(key)) {
        fieldMap.set(key, { values: new Set(), types: new Set(), maxLen: 0 });
      }
      const entry = fieldMap.get(key)!;

      const valueType = Array.isArray(value) ? 'array' : typeof value;
      entry.types.add(valueType);

      // Collect sample values (limit per field)
      if (entry.values.size < 5) {
        if (Array.isArray(value)) {
          // For arrays, take first 3 elements
          for (const v of value.slice(0, 3)) {
            entry.values.add(String(v));
          }
        } else if (value !== null && value !== undefined) {
          const str = String(value);
          // Truncate very long values
          entry.values.add(str.length > 100 ? str.slice(0, 100) + '...' : str);
          entry.maxLen = Math.max(entry.maxLen, str.length);
        }
      }
    }
  }

  const fields: FieldSummary[] = [];
  for (const [name, entry] of fieldMap) {
    const primaryType = entry.types.has('array')
      ? 'array'
      : entry.types.has('number')
        ? 'number'
        : entry.types.has('boolean')
          ? 'boolean'
          : entry.types.has('object')
            ? 'object'
            : 'string';

    let arrayElementType: string | undefined;
    if (primaryType === 'array') {
      const sampleValues = Array.from(entry.values);
      if (sampleValues.length > 0) {
        arrayElementType = isNaN(Number(sampleValues[0])) ? 'string' : 'number';
      }
    }

    fields.push({
      name,
      type: primaryType,
      arrayElementType,
      sampleValues: Array.from(entry.values).slice(0, 5),
      uniqueCount: entry.values.size,
      isLongText: entry.maxLen > 100,
    });
  }

  return {
    fieldCount: fields.length,
    sampleCount: samples.length,
    fields,
  };
}

// ─── LLM Prompt ─────────────────────────────────────────────────────────

function buildSystemPrompt(schema: SchemaSummary): string {
  return `You are a JSON schema analyzer for a search system. Your job is to analyze JSON record fields and map the IMPORTANT ones to our canonical search fields so they become filterable and searchable.

## Available Canonical Slots

Core fields (use these first when the JSON field semantically matches):
- title (text) — product/document title
- description (text) — detailed description or summary
- content_summary (text) — short summary
- category (keyword) — main classification
- tags (keyword) — labels/tags
- author (keyword) — creator
- status (keyword) — lifecycle status

Custom slots (use for domain-specific fields):
- custom_string_1 through custom_string_${MAX_CUSTOM_STRINGS} (keyword — exact match, filterable)
- custom_number_1 through custom_number_${MAX_CUSTOM_NUMBERS} (float — range queries, sortable)
- custom_date_1 through custom_date_${MAX_CUSTOM_DATES} (date — range queries)

## Rules

1. Map fields that users would realistically FILTER or SORT by (brand, color, price, size, gender, etc.)
2. Skip internal/system fields (IDs, SKUs, URLs, timestamps, large recommendation arrays)
3. Skip fields with very high cardinality text (descriptions, features lists) — these are for full-text search, not filtering
4. For arrays of short strings (e.g., ["Shirts", "Clothing"]), map to keyword — the first value will be used for filtering
5. For number fields stored as strings (e.g., price: "1050"), map to custom_number slot
6. Use core fields when they match (title→title, description→description, category→category)
7. Provide a clear, concise alias (human-friendly name), 2-4 synonyms (alternative names users might search by), and description for each mapped field
8. Maximum ${MAX_CUSTOM_STRINGS} string + ${MAX_CUSTOM_NUMBERS} number + ${MAX_CUSTOM_DATES} date custom slots

## Output Format

Respond with ONLY a JSON object (no markdown fences):

{
  "mappings": [
    {
      "sourceField": "original JSON field name",
      "canonicalField": "canonical slot name (e.g., custom_string_1, title, category)",
      "type": "keyword|number|text|date",
      "filterable": true,
      "sortable": false,
      "aggregatable": true,
      "alias": "human-friendly name for this field",
      "synonyms": ["synonym1", "synonym2", "synonym3"],
      "description": "what this field represents"
    }
  ],
  "skipped": [
    { "field": "fieldName", "reason": "why this field was skipped" }
  ]
}`;
}

// ─── Response Parsing ───────────────────────────────────────────────────

function parseLlmResponse(response: string): JsonSchemaMappingResult {
  try {
    // Extract JSON from response (handle markdown code fences if present)
    const jsonMatch = response.match(/```json\n([\s\S]*?)\n```/) || response.match(/\{[\s\S]*\}/);

    if (!jsonMatch) {
      logger.warn('No valid JSON found in LLM response for schema mapping');
      return { mappings: [], skippedFields: [], generatedByLlm: false };
    }

    const jsonText = jsonMatch[1] || jsonMatch[0];
    const parsed = JSON.parse(jsonText);

    if (!parsed.mappings || !Array.isArray(parsed.mappings)) {
      logger.warn('Invalid schema mapping response: missing mappings array');
      return { mappings: [], skippedFields: [], generatedByLlm: false };
    }

    // Track used slots to prevent duplicates
    const usedSlots = new Set<string>();
    let nextStringSlot = 1;
    let nextNumberSlot = 1;

    const validMappings: FieldMapping[] = [];

    for (const mapping of parsed.mappings) {
      if (!mapping.sourceField || !mapping.canonicalField) continue;

      const canonicalField = String(mapping.canonicalField);

      // Validate canonical field is a real slot
      const isValid =
        canonicalField in CORE_FIELD_SLOTS ||
        /^custom_string_\d+$/.test(canonicalField) ||
        /^custom_number_\d+$/.test(canonicalField) ||
        /^custom_date_\d+$/.test(canonicalField);

      if (!isValid) {
        logger.warn('LLM returned invalid canonical field, skipping', { field: canonicalField });
        continue;
      }

      // Prevent duplicate slot assignment
      if (usedSlots.has(canonicalField)) {
        // Auto-assign next available slot
        const slotType = mapping.type === 'number' ? 'number' : 'string';
        const reassigned = assignNextSlot(slotType, nextStringSlot, nextNumberSlot);
        if (!reassigned) continue;

        if (slotType === 'number') {
          nextNumberSlot = reassigned.nextSlot;
        } else {
          nextStringSlot = reassigned.nextSlot;
        }

        mapping.canonicalField = reassigned.slot;
      }

      usedSlots.add(mapping.canonicalField);

      // Track slot counters for custom fields
      const stringMatch = canonicalField.match(/^custom_string_(\d+)$/);
      const numberMatch = canonicalField.match(/^custom_number_(\d+)$/);
      if (stringMatch) nextStringSlot = Math.max(nextStringSlot, parseInt(stringMatch[1], 10) + 1);
      if (numberMatch) nextNumberSlot = Math.max(nextNumberSlot, parseInt(numberMatch[1], 10) + 1);

      // Build synonyms: merge LLM synonyms + sourceField (if different from alias)
      const rawSynonyms: string[] = Array.isArray(mapping.synonyms)
        ? mapping.synonyms.map(String).filter(Boolean)
        : [];
      const alias = String(mapping.alias || mapping.sourceField);
      // Always include the raw source field as a synonym so the LLM can resolve it
      if (mapping.sourceField && String(mapping.sourceField) !== alias) {
        rawSynonyms.push(String(mapping.sourceField));
      }
      // Deduplicate and remove the alias itself
      const synonyms = [...new Set(rawSynonyms)].filter((s) => s !== alias);

      validMappings.push({
        sourceField: String(mapping.sourceField),
        canonicalField: String(mapping.canonicalField),
        type: validateType(mapping.type),
        filterable: mapping.filterable !== false,
        sortable: mapping.sortable === true,
        aggregatable: mapping.aggregatable === true,
        alias,
        synonyms,
        description: String(mapping.description || ''),
        sampleValues: mapping.sampleValues,
      });
    }

    const skippedFields = (parsed.skipped || []).map((s: any) => ({
      field: String(s.field || ''),
      reason: String(s.reason || 'Unknown'),
    }));

    return {
      mappings: validMappings,
      skippedFields,
      generatedByLlm: true,
    };
  } catch (error) {
    logger.error('Failed to parse LLM schema mapping response', {
      error: error instanceof Error ? error.message : String(error),
    });
    return { mappings: [], skippedFields: [], generatedByLlm: false };
  }
}

// ─── Helpers ────────────────────────────────────────────────────────────

function validateType(type: string): 'keyword' | 'number' | 'text' | 'date' {
  if (type === 'keyword' || type === 'number' || type === 'text' || type === 'date') {
    return type;
  }
  return 'keyword';
}

function assignNextSlot(
  slotType: 'string' | 'number',
  nextString: number,
  nextNumber: number,
): { slot: string; nextSlot: number } | null {
  if (slotType === 'number') {
    if (nextNumber > MAX_CUSTOM_NUMBERS) return null;
    return { slot: `custom_number_${nextNumber}`, nextSlot: nextNumber + 1 };
  }
  if (nextString > MAX_CUSTOM_STRINGS) return null;
  return { slot: `custom_string_${nextString}`, nextSlot: nextString + 1 };
}

/**
 * Canonical fields that are typed as numeric (integer or float) in OpenSearch.
 * Values mapped to these must be valid numbers — string/URL values
 * will cause OpenSearch mapper_parsing_exception at index time.
 * Incident: product_image (a URL) was mapped to attachment_count (integer),
 * causing all 5 chunks to fail bulk upsert.
 */
const NUMERIC_CANONICAL_FIELDS = new Set([
  'attachment_count',
  'comment_count',
  // custom_number_1..10 are float type
  ...Array.from({ length: 10 }, (_, i) => `custom_number_${i + 1}`),
]);

/**
 * Build canonical metadata for a single JSON record using the field mapping.
 *
 * Includes type-safety validation against OpenSearch field types to prevent
 * mapper_parsing_exception errors when bad mappings store strings into integer
 * fields or vice versa.
 *
 * @param record - The parsed JSON record
 * @param mappings - Field mappings from LLM analysis
 * @returns Canonical metadata object to set on the SearchChunk
 */
/**
 * Resolve a dot-notation path (e.g., "productSpecs.battery") to a value in a nested object.
 * Returns undefined if any segment along the path is missing.
 */
function getNestedValue(obj: Record<string, unknown>, path: string): unknown {
  // Fast path: top-level key (no dots)
  if (!path.includes('.')) return obj[path];

  const segments = path.split('.');
  let current: unknown = obj;
  for (const segment of segments) {
    if (current === null || current === undefined || typeof current !== 'object') {
      return undefined;
    }
    current = (current as Record<string, unknown>)[segment];
  }
  return current;
}

export function buildCanonicalMetadataFromMapping(
  record: Record<string, unknown>,
  mappings: FieldMapping[],
): Record<string, unknown> {
  const canonical: Record<string, unknown> = {};

  for (const mapping of mappings) {
    const value = getNestedValue(record, mapping.sourceField);
    if (value === null || value === undefined) continue;

    // ── Type-safety guard: numeric canonical fields MUST receive numbers.
    //    If the mapping erroneously points a string/URL source to a numeric
    //    canonical slot, skip it rather than crash the entire OpenSearch bulk upsert.
    if (NUMERIC_CANONICAL_FIELDS.has(mapping.canonicalField)) {
      const num = typeof value === 'number' ? value : parseFloat(String(value));
      if (!isNaN(num) && isFinite(num)) {
        canonical[mapping.canonicalField] = num;
      }
      // Skip non-numeric values silently — bad mapping, not bad data
      continue;
    }

    if (mapping.type === 'number') {
      // Coerce to number
      const num = typeof value === 'number' ? value : parseFloat(String(value));
      if (!isNaN(num)) {
        canonical[mapping.canonicalField] = num;
      }
    } else if (Array.isArray(value)) {
      // Keyword fields in OpenSearch natively support arrays — store all
      // elements so term/terms filters can match any value in the array.
      const stringValues = value.filter((v) => v !== null && v !== undefined).map((v) => String(v));
      if (stringValues.length > 0) {
        canonical[mapping.canonicalField] = stringValues;
      }
    } else {
      canonical[mapping.canonicalField] = String(value);
    }
  }

  return canonical;
}
