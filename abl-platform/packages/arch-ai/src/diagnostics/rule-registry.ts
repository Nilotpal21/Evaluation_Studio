/**
 * Rule registry — maps diagnostic code → description, impact, severity, fix hint.
 *
 * Every rule from Appendix A.2 of the design spec is registered here.
 * Validators reference these codes when emitting findings.
 */

import type { RuleEntry, DiagnosticCategory, DiagnosticSeverity } from './types.js';

function r(
  code: string,
  description: string,
  impact: string,
  severity: DiagnosticSeverity,
  category: DiagnosticCategory,
  fixEffort: 'S' | 'M' | 'L',
): RuleEntry {
  return { code, description, impact, severity, category, fixEffort };
}

