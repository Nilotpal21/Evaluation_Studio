/** Indexed registry — O(1) lookup by code. */
const RULE_MAP = new Map<string, RuleEntry>();
for (const rule of RULES) {
  RULE_MAP.set(rule.code, rule);
}

/** Look up a rule by code. Returns undefined for unknown codes. */
export function getRule(code: string): RuleEntry | undefined {
  return RULE_MAP.get(code);
}

/** Get all rules in a category. */
export function getRulesByCategory(category: DiagnosticCategory): RuleEntry[] {
  return RULES.filter((rule) => rule.category === category);
}

/** Get all registered rules. */
export function getAllRules(): readonly RuleEntry[] {
  return RULES;
}

/** Total registered rules. */
export const RULE_COUNT = RULES.length;
