/**
 * Model Registry — Hyperparameters & Capabilities
 *
 * Source of truth for model hyperparameter definitions, I/O capabilities,
 * and tool support flags. The Studio UI reads this data via the
 * GET /api/model-capabilities/:modelId endpoint to render dynamic controls.
 *
 * To add a model: add an entry to MODEL_REGISTRY below.
 * To update parameters: edit the model's hyperParameters array.
 * To add a provider: add entries and use the provider string consistently.
 *
 * Initial seed: ml-model-config (147 models, 6 providers)
 */

// =============================================================================
// TYPES
// =============================================================================

export interface HyperParameter {
  type: 'rangeSlider' | 'text' | 'textArea' | 'dropdown' | 'radioButton' | 'section' | 'toggle';
  name: string;
  unifiedParam: string;
  displayName: string;
  required: boolean;
  defaultValue?: number | string | boolean;
  min?: number;
  max?: number;
  step?: number;
  description: string;
  valueMap?: string[];
  options?: HyperParameter[];
  /** Nested hyperParameters for section type */
  hyperParameters?: HyperParameter[];
  /** Read-only text fields (e.g. audio format) */
  readonly?: boolean;
  /** Placeholder text for text/textArea inputs */
  placeholder?: string;
}

/**
 * Enhanced Capabilities V2 — Detailed Model Capabilities
 *
 * Replaces simple string[] with structured metadata for JSON-based UI generation.
 * Includes modalities, features, limits, parameter support matrix, reasoning config,
 * pricing, and performance characteristics.
 */
export interface ModelCapabilities {
  // Core capabilities
  modalities: {
    input: Array<'text' | 'image' | 'audio' | 'video' | 'document'>;
    output: Array<'text' | 'image' | 'audio'>;
  };

  // Advanced features
  features: {
    streaming: boolean;
    tools: boolean;
    parallelToolCalls: boolean;
    structuredOutput: boolean;
    realtimeVoice: boolean;
    caching: boolean;
    vision: boolean;
    reasoning: boolean;
    webSearch: boolean;
    citations: boolean;
  };

  // Context and output limits
  limits: {
    contextWindow: number;
    maxOutputTokens: number;
    maxImages?: number;
    maxAudioDuration?: number;
  };

  // Parameter support matrix
  parameterSupport: {
    temperature: boolean;
    temperatureRange?: { min: number; max: number; default: number };
    maxTokens: boolean;
    topP: boolean;
    topK: boolean;
    frequencyPenalty: boolean;
    presencePenalty: boolean;
    stopSequences: boolean;
    seed: boolean;
    logprobs: boolean;
    topLogprobs: boolean;
  };

  // Reasoning configuration (if applicable)
  reasoningConfig?: {
    type: 'effort' | 'thinking' | 'thinkingLevel' | 'thinkingBudget' | 'adaptive';
    effortLevels?: Array<'low' | 'medium' | 'high'>;
    effortDefault?: string;
    thinkingBudgetMin?: number;
    thinkingBudgetDefault?: number;
    thinkingRequired?: boolean;
    thinkingLevels?: Array<'off' | 'low' | 'medium' | 'high'>;
    adaptiveBuiltIn?: boolean; // Claude 4.6
  };

  // Pricing
  pricing?: {
    inputCostPer1k: number;
    outputCostPer1k: number;
    thinkingCostPer1k?: number; // For reasoning models
    cacheCostPer1k?: number; // For prompt caching
  };

  // Performance characteristics
  performance: {
    speed: 'fast' | 'balanced' | 'powerful';
    latencyMs?: number; // Typical first-token latency
    realtimeCapable?: boolean;
  };
}

export interface ModelRegistryEntry {
  provider: string;
  hyperParameters: HyperParameter[];

  // --- Capabilities (V1 - kept for backward compatibility) ---
  capabilities: string[]; // ["textToText", "imageToText", ...]
  supportsTools: boolean;
  supportsParallelToolCalls: boolean;
  supportsStructuredOutput: boolean;

  // --- Display & context metadata ---
  displayName: string; // "GPT-4o", "Claude Opus 4" — human-readable
  contextWindow: number; // Input context window size (tokens)
  maxOutputTokens: number; // Maximum output tokens

  // --- Enhanced Capabilities (V2 - new structured metadata) ---
  capabilitiesV2?: ModelCapabilities; // Optional during migration, will become required

  // --- Capability flags (optional, defaults to false/true) ---
  supportsStreaming?: boolean; // default true
  supportsRealtimeVoice?: boolean;
  isReasoningModel?: boolean;
  supportsReasoningEffort?: boolean;
  supportsThinking?: boolean;
  supportsThinkingBudget?: boolean;
  temperatureDisabled?: boolean;
  topPDisabled?: boolean;

  // --- Provider-specific API support ---
  /** OpenAI only: true = use Responses API, false/undefined = use Chat Completions API */
  supportsResponsesApi?: boolean;

  // --- Pricing (optional, only for curated models) ---
  pricing?: {
    inputCostPer1k: number;
    outputCostPer1k: number;
  };
}

