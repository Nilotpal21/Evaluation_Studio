/**
 * Check if an OpenAI model supports the Responses API.
 *
 * Resolution order:
 * 1. Explicit `supportsResponsesApi` flag on the model's registry entry (per-model override)
 * 2. Membership in the OPENAI_RESPONSES_API_MODELS set (curated list)
 * 3. Default: false → use Chat Completions API (universally compatible)
 */
export function modelSupportsResponsesApi(modelId: string): boolean {
  const hasProviderPrefix = modelId.includes('/');
  const trimmedModelId = modelId.trim();
  const slashIndex = trimmedModelId.lastIndexOf('/');
  const bareModelId = slashIndex === -1 ? trimmedModelId : trimmedModelId.slice(slashIndex + 1);
  const normalizedModelId = bareModelId.toLowerCase();
  const entry =
    (hasProviderPrefix ? MODEL_REGISTRY[normalizedModelId] : undefined) ??
    MODEL_REGISTRY[bareModelId] ??
    MODEL_REGISTRY[normalizedModelId];
  if (entry?.supportsResponsesApi !== undefined) {
    return entry.supportsResponsesApi;
  }
  return (
    OPENAI_RESPONSES_API_MODELS.has(normalizedModelId) ||
    /^(?:gpt-5|o[134])(?:[.-]|$)/.test(normalizedModelId)
  );
}

