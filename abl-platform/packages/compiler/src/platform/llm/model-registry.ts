// =============================================================================
// CATALOG BUILDER — converts registry entries to the shape model-catalog needs
// =============================================================================

export interface CatalogModelData {
  modelId: string;
  provider: string;
  displayName: string;
  capabilities: {
    supportsTools: boolean;
    supportsVision: boolean;
    supportsStreaming: boolean;
    supportsRealtimeVoice?: boolean;
    contextWindow: number;
  };
  pricing?: { inputCostPer1k: number; outputCostPer1k: number };
}

const MICROSOFT_FOUNDRY_ANTHROPIC_PROVIDER = 'microsoft_foundry_anthropic';

const PROVIDER_CATALOG_ALIASES = [
  {
    provider: MICROSOFT_FOUNDRY_ANTHROPIC_PROVIDER,
    modelIdPrefix: MICROSOFT_FOUNDRY_ANTHROPIC_PROVIDER,
    models: [
      {
        baseModelId: 'claude-opus-4-7',
        displayName: 'Claude Opus 4.7 (Microsoft Foundry)',
      },
      {
        baseModelId: 'claude-sonnet-4-6-20260217',
        displayName: 'Claude Sonnet 4.6 (Microsoft Foundry)',
      },
      {
        baseModelId: 'claude-sonnet-4-5-20250929',
        displayName: 'Claude Sonnet 4.5 (Microsoft Foundry)',
      },
      {
        baseModelId: 'claude-haiku-4-5-20251001',
        displayName: 'Claude Haiku 4.5 (Microsoft Foundry)',
      },
    ],
  },
] as const;

function toCatalogModelData(
  modelId: string,
  entry: ModelRegistryEntry,
  overrides: Partial<Pick<CatalogModelData, 'provider' | 'displayName'>> = {},
): CatalogModelData {
  return {
    modelId,
    provider: overrides.provider ?? entry.provider,
    displayName: overrides.displayName ?? entry.displayName,
    capabilities: {
      supportsTools: entry.supportsTools,
      supportsVision: entry.capabilities.includes('imageToText'),
      supportsStreaming: entry.supportsStreaming ?? true,
      supportsRealtimeVoice: entry.supportsRealtimeVoice,
      contextWindow: entry.contextWindow,
    },
    pricing: entry.pricing,
  };
}

export function getBuiltInCatalog(): CatalogModelData[] {
  const registryCatalog = Object.entries(MODEL_REGISTRY).map(([modelId, entry]) =>
    toCatalogModelData(modelId, entry),
  );
  const providerAliasCatalog = PROVIDER_CATALOG_ALIASES.flatMap((alias) =>
    alias.models.map((model) => {
      const entry = MODEL_REGISTRY[model.baseModelId];
      return toCatalogModelData(`${alias.modelIdPrefix}/${model.baseModelId}`, entry, {
        provider: alias.provider,
        displayName: model.displayName,
      });
    }),
  );

  return [...registryCatalog, ...providerAliasCatalog];
}
