function mergeCreatedEvalIds(
  target: CoreImportCreatedEvalIdsV2,
  next: CoreImportCreatedEvalIdsV2,
): void {
  for (const [collection, ids] of Object.entries(next) as Array<
    [keyof CoreImportCreatedEvalIdsV2, string[] | undefined]
  >) {
    if (!ids || ids.length === 0) {
      continue;
    }
    target[collection] = [...(target[collection] ?? []), ...ids];
  }
}

function countCreatedEvalIds(ids: CoreImportCreatedEvalIdsV2): number {
  return Object.values(ids).reduce((total, values) => total + (values?.length ?? 0), 0);
}

function requireEvalAdapterMethod<T extends keyof CoreImportApplyAdapterV2>(
  adapter: CoreImportApplyAdapterV2,
  method: T,
): NonNullable<CoreImportApplyAdapterV2[T]> {
  const value = adapter[method];
  if (!value) {
    throw new Error(`Import adapter does not implement ${String(method)}`);
  }
  return value as NonNullable<CoreImportApplyAdapterV2[T]>;
}

export async function executeCoreImportApplyPlanV2(
  plan: CoreImportApplyPlanV2,
  adapter: CoreImportApplyAdapterV2,
): Promise<CoreImportApplyExecutionResultV2> {
  const createdPromptIds: string[] = [];
  const createdAgentIds: string[] = [];
  const createdToolIds: string[] = [];
  const createdMcpServerIds: string[] = [];
  const createdLocaleIds: string[] = [];
  const createdProfileIds: string[] = [];
  const createdEvalIds: CoreImportCreatedEvalIdsV2 = {};

  try {
    const createPrompts = plan.promptOperations.filter(isCreatePrompt);
    const updatePrompts = plan.promptOperations.filter(isUpdatePrompt);
    const deletePromptIds = plan.promptOperations
      .filter((operation) => operation.type === 'delete')
      .map((operation) => operation.promptId);

    const createAgents = plan.agentOperations.filter(isCreateAgent);
    const updateAgents = plan.agentOperations.filter(isUpdateAgent);
    const deleteAgentNames = plan.agentOperations
      .filter((operation) => operation.type === 'delete')
      .map((operation) => operation.agentName);

    const createMcpServers = plan.mcpServerOperations.filter(isCreateMcpServer);
    const updateMcpServers = plan.mcpServerOperations.filter(isUpdateMcpServer);
    const deleteMcpServerNames = plan.mcpServerOperations
      .filter((operation) => operation.type === 'delete')
      .map((operation) => operation.serverName);

    const createTools = plan.toolOperations.filter(isCreateTool);
    const updateTools = plan.toolOperations.filter(isUpdateTool);
    const deleteToolNames = plan.toolOperations
      .filter((operation) => operation.type === 'delete')
      .map((operation) => operation.toolName);
    const createLocales = plan.localeOperations.filter(isCreateLocale);
    const updateLocales = plan.localeOperations.filter(isUpdateLocale);
    const deleteLocalePaths = plan.localeOperations
      .filter((operation) => operation.type === 'delete')
      .map((operation) => operation.relativePath);
    const createProfiles = plan.profileOperations.filter(isCreateProfile);
    const updateProfiles = plan.profileOperations.filter(isUpdateProfile);
    const deleteProfileNames = plan.profileOperations
      .filter((operation) => operation.type === 'delete')
      .map((operation) => operation.profileName);
    const upsertModelPolicies = (plan.modelPolicyOperations ?? []).filter(isUpsertModelPolicy);
    const deleteModelPolicies = (plan.modelPolicyOperations ?? []).filter(
      (operation) => operation.type === 'delete',
    );
    const createEvalDependencies = plan.evalOperations
      .filter(isCreateEval)
      .filter((operation) => operation.collection !== 'eval_sets');
    const updateEvalDependencies = plan.evalOperations
      .filter(isUpdateEval)
      .filter((operation) => operation.collection !== 'eval_sets');
    const createEvalSets = plan.evalOperations
      .filter(isCreateEval)
      .filter((operation) => operation.collection === 'eval_sets');
    const updateEvalSets = plan.evalOperations
      .filter(isUpdateEval)
      .filter((operation) => operation.collection === 'eval_sets');
    const deleteEvalSets = plan.evalOperations.filter(
      (operation) => operation.type === 'delete' && operation.collection === 'eval_sets',
    );
    const deleteEvalDependencies = plan.evalOperations.filter(
      (operation) => operation.type === 'delete' && operation.collection !== 'eval_sets',
    );
    const hasDraftGraphMutation =
      createPrompts.length > 0 ||
      updatePrompts.length > 0 ||
      deletePromptIds.length > 0 ||
      createAgents.length > 0 ||
      updateAgents.length > 0 ||
      deleteAgentNames.length > 0 ||
      createMcpServers.length > 0 ||
      updateMcpServers.length > 0 ||
      deleteMcpServerNames.length > 0 ||
      createTools.length > 0 ||
      updateTools.length > 0 ||
      deleteToolNames.length > 0 ||
      createLocales.length > 0 ||
      updateLocales.length > 0 ||
      deleteLocalePaths.length > 0 ||
      createProfiles.length > 0 ||
      updateProfiles.length > 0 ||
      deleteProfileNames.length > 0;

    if (createPrompts.length > 0) {
      createdPromptIds.push(
        ...(await executeApplyStage('create_prompts', () => adapter.createPrompts(createPrompts))),
      );
    }
    if (updatePrompts.length > 0) {
      await executeApplyStage('update_prompts', () => adapter.updatePrompts(updatePrompts));
    }
    if (deletePromptIds.length > 0) {
      await executeApplyStage('delete_prompts', () => adapter.deletePrompts(deletePromptIds));
    }

    if (createProfiles.length > 0) {
      createdProfileIds.push(
        ...(await executeApplyStage('create_profiles', () =>
          adapter.createProfiles(createProfiles),
        )),
      );
    }
    if (updateProfiles.length > 0) {
      await executeApplyStage('update_profiles', () => adapter.updateProfiles(updateProfiles));
    }
    if (createAgents.length > 0) {
      createdAgentIds.push(
        ...(await executeApplyStage('create_agents', () => adapter.createAgents(createAgents))),
      );
    }
    if (updateAgents.length > 0) {
      await executeApplyStage('update_agents', () => adapter.updateAgents(updateAgents));
    }
    if (deleteAgentNames.length > 0) {
      await executeApplyStage('delete_agents', () => adapter.deleteAgents(deleteAgentNames));
    }
    if (createMcpServers.length > 0) {
      createdMcpServerIds.push(
        ...(await executeApplyStage('create_mcp_servers', () =>
          adapter.createMcpServers(createMcpServers),
        )),
      );
    }
    if (updateMcpServers.length > 0) {
      await executeApplyStage('update_mcp_servers', () =>
        adapter.updateMcpServers(updateMcpServers),
      );
    }
    if (deleteMcpServerNames.length > 0) {
      await executeApplyStage('delete_mcp_servers', () =>
        adapter.deleteMcpServers(deleteMcpServerNames),
      );
    }

    if (createTools.length > 0) {
      createdToolIds.push(
        ...(await executeApplyStage('create_tools', () => adapter.createTools(createTools))),
      );
    }
    if (updateTools.length > 0) {
      await executeApplyStage('update_tools', () => adapter.updateTools(updateTools));
    }
    if (deleteToolNames.length > 0) {
      await executeApplyStage('delete_tools', () => adapter.deleteTools(deleteToolNames));
    }
    if (createLocales.length > 0) {
      createdLocaleIds.push(
        ...(await executeApplyStage('create_locales', () => adapter.createLocales(createLocales))),
      );
    }
    if (updateLocales.length > 0) {
      await executeApplyStage('update_locales', () => adapter.updateLocales(updateLocales));
    }
    if (deleteLocalePaths.length > 0) {
      await executeApplyStage('delete_locales', () => adapter.deleteLocales(deleteLocalePaths));
    }
    if (deleteProfileNames.length > 0) {
      await executeApplyStage('delete_profiles', () => adapter.deleteProfiles(deleteProfileNames));
    }
    if (hasDraftGraphMutation && adapter.refreshAgentDraftMetadata) {
      await executeApplyStage('refresh_agent_draft_metadata', () =>
        adapter.refreshAgentDraftMetadata!(),
      );
    }
    if (upsertModelPolicies.length > 0) {
      const upsertModelPolicyConfigs = requireEvalAdapterMethod(
        adapter,
        'upsertModelPolicyConfigs',
      );
      await executeApplyStage('upsert_model_policies', () =>
        upsertModelPolicyConfigs.call(adapter, upsertModelPolicies),
      );
    }
    if (deleteModelPolicies.length > 0) {
      const deleteModelPolicyConfigs = requireEvalAdapterMethod(
        adapter,
        'deleteModelPolicyConfigs',
      );
      await executeApplyStage('delete_model_policies', () =>
        deleteModelPolicyConfigs.call(adapter, deleteModelPolicies),
      );
    }
    if (createEvalDependencies.length > 0) {
      const createEvalRecords = requireEvalAdapterMethod(adapter, 'createEvalRecords');
      mergeCreatedEvalIds(
        createdEvalIds,
        await executeApplyStage('create_evals', () =>
          createEvalRecords.call(adapter, createEvalDependencies),
        ),
      );
    }
    if (updateEvalDependencies.length > 0) {
      const updateEvalRecords = requireEvalAdapterMethod(adapter, 'updateEvalRecords');
      await executeApplyStage('update_evals', () =>
        updateEvalRecords.call(adapter, updateEvalDependencies),
      );
    }
    if (createEvalSets.length > 0) {
      const createEvalRecords = requireEvalAdapterMethod(adapter, 'createEvalRecords');
      mergeCreatedEvalIds(
        createdEvalIds,
        await executeApplyStage('create_eval_sets', () =>
          createEvalRecords.call(adapter, createEvalSets),
        ),
      );
    }
    if (updateEvalSets.length > 0) {
      const updateEvalRecords = requireEvalAdapterMethod(adapter, 'updateEvalRecords');
      await executeApplyStage('update_eval_sets', () =>
        updateEvalRecords.call(adapter, updateEvalSets),
      );
    }
    if (deleteEvalSets.length > 0) {
      const deleteEvalRecords = requireEvalAdapterMethod(adapter, 'deleteEvalRecords');
      await executeApplyStage('delete_eval_sets', () =>
        deleteEvalRecords.call(adapter, deleteEvalSets),
      );
    }
    if (deleteEvalDependencies.length > 0) {
      const deleteEvalRecords = requireEvalAdapterMethod(adapter, 'deleteEvalRecords');
      await executeApplyStage('delete_evals', () =>
        deleteEvalRecords.call(adapter, deleteEvalDependencies),
      );
    }

    await executeApplyStage('set_entry_agent', () => adapter.setEntryAgent(plan.entryAgentName));

    return {
      success: true,
      applied: plan.applied,
      entryAgentName: plan.entryAgentName,
    };
  } catch (error) {
    if (
      createdPromptIds.length > 0 ||
      createdAgentIds.length > 0 ||
      createdToolIds.length > 0 ||
      createdMcpServerIds.length > 0 ||
      createdLocaleIds.length > 0 ||
      createdProfileIds.length > 0 ||
      countCreatedEvalIds(createdEvalIds) > 0
    ) {
      try {
        await adapter.rollbackCreated(
          createdPromptIds,
          createdAgentIds,
          createdToolIds,
          createdMcpServerIds,
          createdLocaleIds,
          createdProfileIds,
          createdEvalIds,
        );
      } catch (rollbackError) {
        log.error('Core import rollback failed', {
          createdPromptIds,
          createdAgentIds,
          createdToolIds,
          createdMcpServerIds,
          createdLocaleIds,
          createdProfileIds,
          createdEvalCount: countCreatedEvalIds(createdEvalIds),
          error: rollbackError instanceof Error ? rollbackError.message : String(rollbackError),
        });
      }
    }

    log.error('Core import apply execution failed', {
      stage: error instanceof CoreImportApplyStageError ? error.stage : 'unknown',
      sanitizedCause:
        error instanceof CoreImportApplyStageError
          ? error.sanitizedCause
          : 'Persistence operation failed',
      createdPromptCount: createdPromptIds.length,
      createdAgentCount: createdAgentIds.length,
      createdToolCount: createdToolIds.length,
      createdMcpServerCount: createdMcpServerIds.length,
      createdLocaleCount: createdLocaleIds.length,
      createdProfileCount: createdProfileIds.length,
      createdEvalCount: countCreatedEvalIds(createdEvalIds),
      error: error instanceof Error ? error.message : String(error),
    });

    return {
      success: false,
      error: {
        code: 'IMPORT_APPLY_FAILED',
        message: 'Import failed during apply. Created records have been rolled back.',
        ...(error instanceof CoreImportApplyStageError ? { stage: error.stage } : {}),
        sanitizedCause:
          error instanceof CoreImportApplyStageError
            ? error.sanitizedCause
            : sanitizeImportApplyCause(error),
      },
    };
  }
}
