function buildPromptOperations(input: {
  existingState: ExistingProjectStateV2;
  importedPrompts: Map<
    string,
    {
      bundle: ProjectIOPromptLibraryBundle;
      sourceFile: string;
      sourceContent: string;
    }
  >;
  deleteUnmatched: boolean;
}): CoreImportPromptOperationV2[] {
  const operations: CoreImportPromptOperationV2[] = [];
  const existingPrompts =
    input.existingState.prompts ?? new Map<string, ProjectIOPromptLibraryBundle>();

  for (const [promptId, imported] of input.importedPrompts) {
    const existing = existingPrompts.get(promptId);
    const sourceHash = computeSourceHash(imported.sourceContent);

    if (!existing) {
      operations.push({
        type: 'create',
        promptId,
        promptName: imported.bundle.name,
        bundle: imported.bundle,
        sourceHash,
        sourceFile: imported.sourceFile,
      });
      continue;
    }

    if (
      serializePromptLibraryBundleForFile(existing) !==
      serializePromptLibraryBundleForFile(imported.bundle)
    ) {
      operations.push({
        type: 'update',
        promptId,
        promptName: imported.bundle.name,
        bundle: imported.bundle,
        sourceHash,
        sourceFile: imported.sourceFile,
      });
    }
  }

  if (!input.deleteUnmatched) {
    return operations;
  }

  for (const [promptId, existing] of existingPrompts) {
    if (!input.importedPrompts.has(promptId)) {
      operations.push({
        type: 'delete',
        promptId,
        promptName: existing.name,
        bundle: null,
        sourceHash: null,
        sourceFile: null,
      });
    }
  }

  return operations;
}

function removePromptVersionEntries(
  versions: Map<string, { promptId: string; versionId: string; status: string | null }>,
  promptId: string,
): void {
  for (const key of versions.keys()) {
    if (key.startsWith(`${promptId}:`)) {
      versions.delete(key);
    }
  }
}

function addPromptBundleVersions(
  versions: Map<string, { promptId: string; versionId: string; status: string | null }>,
  bundle: ProjectIOPromptLibraryBundle,
): void {
  for (const version of bundle.versions) {
    versions.set(`${bundle.promptId}:${version.versionId}`, {
      promptId: bundle.promptId,
      versionId: version.versionId,
      status: version.status ?? null,
    });
  }
}

function validateAgentPromptLibraryRefs(input: {
  existingState: ExistingProjectStateV2;
  agentOperations: CoreImportAgentOperationV2[];
  promptOperations: CoreImportPromptOperationV2[];
}): { success: true } | { success: false; message: string } {
  const availablePromptVersions = new Map<
    string,
    { promptId: string; versionId: string; status: string | null }
  >();
  for (const bundle of input.existingState.prompts?.values() ?? []) {
    addPromptBundleVersions(availablePromptVersions, bundle);
  }

  for (const operation of input.promptOperations) {
    removePromptVersionEntries(availablePromptVersions, operation.promptId);
    if (operation.type !== 'delete' && operation.bundle) {
      addPromptBundleVersions(availablePromptVersions, operation.bundle);
    }
  }

  const finalAgentRefs = new Map<string, AgentPromptLibraryRefSnapshot | null>();
  for (const [agentName, agent] of input.existingState.agents.entries()) {
    finalAgentRefs.set(agentName, agent.systemPromptLibraryRef ?? null);
  }

  for (const operation of input.agentOperations) {
    if (operation.type === 'delete') {
      finalAgentRefs.delete(operation.agentName);
      continue;
    }
    finalAgentRefs.set(operation.agentName, operation.systemPromptLibraryRef ?? null);
  }

  for (const [agentName, ref] of finalAgentRefs) {
    if (!ref) {
      continue;
    }

    const version = availablePromptVersions.get(`${ref.promptId}:${ref.versionId}`);
    if (!version || version.status === 'archived') {
      return {
        success: false,
        message: `Imported agent "${agentName}" references prompt version "${ref.promptId}/${ref.versionId}" that is not available after import. Include the prompts layer or choose an available prompt version before importing.`,
      };
    }
  }

  return { success: true };
}

function countOperations(
  promptOperations: CoreImportPromptOperationV2[],
  agentOperations: CoreImportAgentOperationV2[],
  toolOperations: CoreImportToolOperationV2[],
  localeOperations: CoreImportLocaleOperationV2[],
  profileOperations: CoreImportProfileOperationV2[],
  modelPolicyOperations: CoreImportModelPolicyOperationV2[] = [],
  evalOperations: CoreImportEvalOperationV2[] = [],
): CoreImportApplyCountsV2 {
  const evalCounts = countEvalOperations(evalOperations);
  const hasEvalOperations =
    evalCounts.evalsCreated > 0 || evalCounts.evalsUpdated > 0 || evalCounts.evalsDeleted > 0;
  const modelPoliciesUpserted = modelPolicyOperations.filter(
    (operation) => operation.type === 'upsert',
  ).length;
  const modelPoliciesDeleted = modelPolicyOperations.filter(
    (operation) => operation.type === 'delete',
  ).length;

  return {
    created: agentOperations.filter((operation) => operation.type === 'create').length,
    updated: agentOperations.filter((operation) => operation.type === 'update').length,
    deleted: agentOperations.filter((operation) => operation.type === 'delete').length,
    ...(promptOperations.length > 0
      ? {
          promptsCreated: promptOperations.filter((operation) => operation.type === 'create')
            .length,
          promptsUpdated: promptOperations.filter((operation) => operation.type === 'update')
            .length,
          promptsDeleted: promptOperations.filter((operation) => operation.type === 'delete')
            .length,
        }
      : {}),
    toolsCreated: toolOperations.filter((operation) => operation.type === 'create').length,
    toolsUpdated: toolOperations.filter((operation) => operation.type === 'update').length,
    toolsDeleted: toolOperations.filter((operation) => operation.type === 'delete').length,
    localesCreated: localeOperations.filter((operation) => operation.type === 'create').length,
    localesUpdated: localeOperations.filter((operation) => operation.type === 'update').length,
    localesDeleted: localeOperations.filter((operation) => operation.type === 'delete').length,
    profilesCreated: profileOperations.filter((operation) => operation.type === 'create').length,
    profilesUpdated: profileOperations.filter((operation) => operation.type === 'update').length,
    profilesDeleted: profileOperations.filter((operation) => operation.type === 'delete').length,
    ...(hasEvalOperations ? evalCounts : {}),
    ...(modelPoliciesUpserted > 0 || modelPoliciesDeleted > 0
      ? { modelPoliciesUpserted, modelPoliciesDeleted }
      : {}),
  };
}

async function normalizeToolWriteOperationsForPersistence(input: {
  tenantId: string;
  projectId: string;
  operations: CoreImportToolOperationV2[];
  validateToolBindingForSave?: CoreImportToolBindingSaveValidatorV2;
}): Promise<
  | {
      success: true;
      operations: CoreImportToolOperationV2[];
    }
  | {
      success: false;
      message: string;
    }
> {
  const operations: CoreImportToolOperationV2[] = [];

  for (const operation of input.operations) {
    if (operation.type === 'delete') {
      operations.push(operation);
      continue;
    }

    if (!operation.toolType || !isProjectToolType(operation.toolType)) {
      return {
        success: false,
        message: `Imported tool "${operation.toolName}" has unsupported toolType "${operation.toolType ?? 'unknown'}".`,
      };
    }

    if (!operation.dslContent) {
      return {
        success: false,
        message: `Imported tool "${operation.toolName}" is missing dslContent.`,
      };
    }

    const prepared = prepareProjectToolDslForPersistence({
      tenantId: input.tenantId,
      projectId: input.projectId,
      name: operation.toolName,
      toolType: operation.toolType,
      dslContent: operation.dslContent,
    });

    if (!prepared.valid) {
      return {
        success: false,
        message: `Imported tool "${operation.toolName}" is invalid: ${prepared.message}`,
      };
    }

    const requiresReferentialValidation =
      operation.toolType === 'workflow' || operation.toolType === 'searchai';
    if (requiresReferentialValidation && !input.validateToolBindingForSave) {
      return {
        success: false,
        message: `Imported tool "${operation.toolName}" requires referential binding validation for toolType "${operation.toolType}".`,
      };
    }

    if (input.validateToolBindingForSave) {
      const bindingValidation = await input.validateToolBindingForSave({
        tenantId: input.tenantId,
        projectId: input.projectId,
        operationType: operation.type,
        toolName: operation.toolName,
        toolType: operation.toolType,
        dslContent: prepared.dslContent,
        sourceFile: operation.sourceFile,
        autogenerated: operation.autogenerated,
      });

      if (!bindingValidation.valid) {
        return {
          success: false,
          message: `Imported tool "${operation.toolName}" is invalid: ${bindingValidation.message}`,
        };
      }
    }

    operations.push({
      ...operation,
      dslContent: prepared.dslContent,
      sourceHash: prepared.sourceHash,
    });
  }

  return { success: true, operations };
}

async function validateRuntimeConfigWriteOperations(input: {
  tenantId: string;
  projectId: string;
  operations: CoreImportModelPolicyOperationV2[];
  validateRuntimeConfigForSave?: CoreImportRuntimeConfigSaveValidatorV2;
}): Promise<
  | {
      success: true;
      operations: CoreImportModelPolicyOperationV2[];
    }
  | {
      success: false;
      message: string;
    }
> {
  if (!input.validateRuntimeConfigForSave) {
    return { success: true, operations: input.operations };
  }

  const operations: CoreImportModelPolicyOperationV2[] = [];
  for (const operation of input.operations) {
    if (operation.type !== 'upsert' || operation.configType !== 'runtime' || !operation.data) {
      operations.push(operation);
      continue;
    }

    const validation = await input.validateRuntimeConfigForSave({
      tenantId: input.tenantId,
      projectId: input.projectId,
      data: operation.data,
      sourceFile: operation.sourceFile,
    });

    if (!validation.valid) {
      return {
        success: false,
        message: validation.message,
      };
    }

    const data = validation.data ?? operation.data;
    operations.push({
      ...operation,
      data,
      sourceHash:
        data === operation.data
          ? operation.sourceHash
          : computeSourceHash(stableConfigStringify(data)),
    });
  }

  return { success: true, operations };
}

