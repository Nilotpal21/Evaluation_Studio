function computeSourceHash(content: string): string {
  return createHash('sha256').update(content, 'utf8').digest('hex');
}

function makeIssueId(input: string): string {
  return createHash('sha256').update(input, 'utf8').digest('hex').slice(0, 12);
}

function dedupe(values: string[]): string[] {
  return [...new Set(values)];
}

function isLayerEnabled(
  requestedLayers: readonly LayerName[] | undefined,
  layer: LayerName,
): boolean {
  return !requestedLayers || requestedLayers.includes(layer);
}

function buildUnsupportedDirectApplyIssue(preview: ImportPreviewV2): ImportPreviewV2 {
  const unsupportedLayers = preview.layers.filter(
    (layer) => !DIRECT_APPLY_SUPPORTED_LAYERS.includes(layer),
  );
  if (unsupportedLayers.length === 0) {
    return preview;
  }

  const issue: ImportIssue = {
    id: makeIssueId(
      JSON.stringify({
        code: 'E_IMPORT_UNSUPPORTED_LAYERS',
        layers: unsupportedLayers,
      }),
    ),
    severity: 'error',
    blocking: true,
    category: 'general',
    code: 'E_IMPORT_UNSUPPORTED_LAYERS',
    message: `This import surface cannot apply layer(s): ${unsupportedLayers.join(', ')}. Re-run with supported layers only or use a staged layered import path.`,
  };
  const issues = [...preview.issues, issue];
  const blockingIssueCount = issues.filter((entry) => entry.blocking).length;
  const nonBlockingIssueCount = issues.length - blockingIssueCount;

  return {
    ...preview,
    valid: false,
    issues,
    hasBlockingIssues: true,
    blockingIssueCount,
    nonBlockingIssueCount,
  };
}

function sanitizeImportApplyCause(error: unknown): string {
  const rawMessage = error instanceof Error ? error.message : String(error);
  const message = rawMessage.trim();

  if (!message) {
    return 'Unexpected persistence failure';
  }

  if (/E11000|duplicate key/i.test(message)) {
    return 'Duplicate record conflict';
  }

  if (/validation failed|ValidationError/i.test(message)) {
    return 'Validation failed while writing imported data';
  }

  if (/Cast to .* failed/i.test(message)) {
    return 'Invalid identifier or reference in imported data';
  }

  if (/timed out|ECONN|network|socket/i.test(message)) {
    return 'Persistence layer connectivity failure';
  }

  return 'Persistence operation failed';
}

class CoreImportApplyStageError extends Error {
  readonly stage: CoreImportApplyStageV2;
  readonly sanitizedCause: string;

  constructor(stage: CoreImportApplyStageV2, error: unknown) {
    super(error instanceof Error ? error.message : String(error));
    this.name = 'CoreImportApplyStageError';
    this.stage = stage;
    this.sanitizedCause = sanitizeImportApplyCause(error);
  }
}

