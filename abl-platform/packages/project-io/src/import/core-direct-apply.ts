function buildModelPolicyOperations(input: {
  existingState: ExistingProjectStateV2;
  importedConfigs: ImportedModelPolicyConfigEntry[];
  deleteUnmatched: boolean;
}): CoreImportModelPolicyOperationV2[] {
  const operations: CoreImportModelPolicyOperationV2[] = [];
  const importedAgentConfigs = new Set<string>();
  const importedProjectConfigs = new Set<string>();

  for (const imported of input.importedConfigs) {
    if (imported.configType === 'runtime') {
      const type = computeModelPolicyOperationType(
        input.existingState.runtimeConfig,
        imported.data,
      );
      if (type) {
        operations.push({ type, ...imported });
      }
      continue;
    }

    if (imported.configType === 'llm') {
      const type = computeModelPolicyOperationType(input.existingState.llmConfig, imported.data);
      if (type) {
        operations.push({ type, ...imported });
      }
      continue;
    }

    if (imported.configType === 'project_model') {
      if (!imported.modelConfigName) {
        continue;
      }
      importedProjectConfigs.add(imported.modelConfigName);
      const existing = input.existingState.projectModelConfigs?.get(imported.modelConfigName)?.data;
      const type = computeModelPolicyOperationType(existing, imported.data);
      if (type) {
        operations.push({ type, ...imported });
      }
      continue;
    }

    if (!imported.agentName) {
      continue;
    }
    importedAgentConfigs.add(imported.agentName);
    const existing = input.existingState.agentModelConfigs?.get(imported.agentName)?.data;
    const type = computeModelPolicyOperationType(existing, imported.data);
    if (type) {
      operations.push({ type, ...imported });
    }
  }

  if (!input.deleteUnmatched) {
    return operations;
  }

  const importedRuntime = input.importedConfigs.some((config) => config.configType === 'runtime');
  if (!importedRuntime && input.existingState.runtimeConfig) {
    operations.push({
      type: 'delete',
      configType: 'runtime',
      data: null,
      sourceFile: null,
      sourceHash: null,
    });
  }

  const importedLlm = input.importedConfigs.some((config) => config.configType === 'llm');
  if (!importedLlm && input.existingState.llmConfig) {
    operations.push({
      type: 'delete',
      configType: 'llm',
      data: null,
      sourceFile: null,
      sourceHash: null,
    });
  }

  for (const [agentName] of input.existingState.agentModelConfigs ?? new Map()) {
    if (!importedAgentConfigs.has(agentName)) {
      operations.push({
        type: 'delete',
        configType: 'agent_model',
        agentName,
        data: null,
        sourceFile: null,
        sourceHash: null,
      });
    }
  }

  for (const [modelConfigName] of input.existingState.projectModelConfigs ?? new Map()) {
    if (!importedProjectConfigs.has(modelConfigName)) {
      operations.push({
        type: 'delete',
        configType: 'project_model',
        modelConfigName,
        data: null,
        sourceFile: null,
        sourceHash: null,
      });
    }
  }

  return operations;
}

function prepareImportFiles(files: Map<string, string>):
  | {
      success: true;
      preparedFiles: Map<string, string>;
      folderResult: FolderReadResultV2;
      requestedEntryAgent: string | null;
      warnings: string[];
      emptyProject: boolean;
    }
  | {
      success: false;
      error: { code: string; message: string };
    } {
  const { files: strippedFiles } = stripCommonPrefix(files);
  const migration = migrateV1ToV2(strippedFiles);

  if (migration.error) {
    return {
      success: false,
      error: {
        code: migration.error.code,
        message: migration.error.message,
      },
    };
  }

  const folderResult = readFolderV2(migration.files);
  const manifestAgentCount = Object.keys(migration.manifest.agents ?? {}).length;
  const manifestToolCount = Object.keys(migration.manifest.tools ?? {}).length;
  const manifestMcpServerCount = migration.manifest.metadata?.required_mcp_servers?.length ?? 0;
  const manifestProfileCount = Object.keys(migration.manifest.behavior_profiles ?? {}).length;
  const mcpServerFileCount = [...folderResult.configFiles.keys()].filter(
    isMcpServerConfigFilePath,
  ).length;
  const modelPolicyFileCount = [...folderResult.configFiles.keys()].filter(
    (filePath) =>
      filePath === RUNTIME_CONFIG_FILE ||
      filePath === LLM_CONFIG_FILE ||
      PROJECT_MODEL_CONFIG_PATTERN.test(filePath) ||
      AGENT_MODEL_CONFIG_PATTERN.test(filePath),
  ).length;
  const emptyProject = isEmptyCoreProject({
    agentFileCount: folderResult.agentFiles.size,
    promptFileCount: folderResult.promptFiles.size,
    toolFileCount: folderResult.toolFiles.size,
    mcpServerFileCount,
    modelPolicyFileCount,
    localeFileCount: folderResult.localeFiles.size,
    profileFileCount: folderResult.profileFiles.size,
    evalFileCount: folderResult.evalFiles.size,
    manifestAgentCount,
    manifestToolCount,
    manifestMcpServerCount,
    manifestProfileCount,
  });

  if (!folderResult.success && !emptyProject) {
    const primaryValidationIssue = folderResult.validationIssues?.[0];
    return {
      success: false,
      error: {
        code: primaryValidationIssue?.code ?? 'FOLDER_READ_FAILED',
        message: folderResult.errors.join('; '),
      },
    };
  }

  return {
    success: true,
    preparedFiles: migration.files,
    folderResult,
    requestedEntryAgent: migration.manifest.entry_agent,
    warnings: migration.warnings,
    emptyProject,
  };
}

function adjustPreviewForPlan(
  preview: ImportPreviewV2,
  promptOperations: CoreImportPromptOperationV2[],
  agentOperations: CoreImportAgentOperationV2[],
  toolOperations: CoreImportToolOperationV2[],
  mcpServerOperations: CoreImportMcpServerOperationV2[],
  localeOperations: CoreImportLocaleOperationV2[],
  profileOperations: CoreImportProfileOperationV2[],
  modelPolicyOperations: CoreImportModelPolicyOperationV2[],
  evalOperations: CoreImportEvalOperationV2[],
  stubWarnings: string[],
): ImportPreviewV2 {
  const createdPrompts = promptOperations.filter((operation) => operation.type === 'create');
  const updatedPrompts = promptOperations.filter((operation) => operation.type === 'update');
  const deletedPrompts = promptOperations.filter((operation) => operation.type === 'delete');
  const createdAgents = agentOperations
    .filter((operation) => operation.type === 'create')
    .map((operation) => operation.agentName);
  const updatedAgentNames = new Set(
    agentOperations
      .filter((operation) => operation.type === 'update')
      .map((operation) => operation.agentName),
  );
  const deletedAgents = agentOperations
    .filter((operation) => operation.type === 'delete')
    .map((operation) => operation.agentName);

  const createdTools = toolOperations
    .filter((operation) => operation.type === 'create')
    .map((operation) => operation.toolName);
  const updatedTools = toolOperations
    .filter((operation) => operation.type === 'update')
    .map((operation) => operation.toolName);
  const deletedTools = toolOperations
    .filter((operation) => operation.type === 'delete')
    .map((operation) => operation.toolName);
  const createdMcpServers = mcpServerOperations.filter((operation) => operation.type === 'create');
  const updatedMcpServers = mcpServerOperations.filter((operation) => operation.type === 'update');
  const deletedMcpServers = mcpServerOperations.filter((operation) => operation.type === 'delete');
  const createdLocales = localeOperations
    .filter((operation) => operation.type === 'create')
    .map((operation) => operation.filePath);
  const updatedLocales = localeOperations
    .filter((operation) => operation.type === 'update')
    .map((operation) => operation.filePath);
  const deletedLocales = localeOperations
    .filter((operation) => operation.type === 'delete')
    .map((operation) => operation.filePath);
  const createdProfiles = profileOperations
    .filter((operation) => operation.type === 'create')
    .map((operation) => operation.filePath);
  const updatedProfiles = profileOperations
    .filter((operation) => operation.type === 'update')
    .map((operation) => operation.filePath);
  const deletedProfiles = profileOperations
    .filter((operation) => operation.type === 'delete')
    .map((operation) => operation.filePath);
  const upsertedModelPolicies = modelPolicyOperations.filter(
    (operation) => operation.type === 'upsert',
  );
  const deletedModelPolicies = modelPolicyOperations.filter(
    (operation) => operation.type === 'delete',
  );
  const evalCounts = countEvalOperations(evalOperations);
  const previewModifiedByName = new Map(
    preview.agentChanges.modified.map((change) => [change.name, change.diff]),
  );
  const metadataOnlyAgentDiff = (agentName: string): ABLDiffResult => ({
    hasChanges: true,
    sections: [
      {
        section: 'METADATA',
        status: 'modified',
        beforeContent: `Prompt companion metadata before import for ${agentName}`,
        afterContent: `Prompt companion metadata after import for ${agentName}`,
      },
    ],
    summary: {
      added: [],
      removed: [],
      modified: ['METADATA'],
      unchanged: [],
    },
  });

  const nextLayerChanges = {
    ...preview.layerChanges,
    ...(preview.layerChanges.prompts || promptOperations.length > 0
      ? {
          prompts: {
            added: createdPrompts.length,
            modified: updatedPrompts.length,
            removed: deletedPrompts.length,
            unchanged: 0,
          },
        }
      : {}),
    ...(preview.layerChanges.core
      ? {
          core: {
            added:
              createdAgents.length +
              createdTools.length +
              createdMcpServers.length +
              createdLocales.length +
              createdProfiles.length,
            modified:
              updatedAgentNames.size +
              updatedTools.length +
              updatedMcpServers.length +
              updatedLocales.length +
              updatedProfiles.length +
              upsertedModelPolicies.length,
            removed:
              deletedAgents.length +
              deletedTools.length +
              deletedMcpServers.length +
              deletedLocales.length +
              deletedProfiles.length +
              deletedModelPolicies.length,
            unchanged: preview.agentChanges.unchanged.length,
          },
        }
      : {}),
    ...(preview.layerChanges.evals || evalOperations.length > 0
      ? {
          evals: {
            added: evalCounts.evalsCreated,
            modified: evalCounts.evalsUpdated,
            removed: evalCounts.evalsDeleted,
            unchanged: 0,
          },
        }
      : {}),
  };

  return {
    ...preview,
    layerChanges: nextLayerChanges,
    agentChanges: {
      added: createdAgents,
      modified: [...updatedAgentNames].map((agentName) => ({
        name: agentName,
        diff: previewModifiedByName.get(agentName) ?? metadataOnlyAgentDiff(agentName),
      })),
      removed: deletedAgents,
      unchanged: preview.agentChanges.unchanged.filter(
        (agentName) => !updatedAgentNames.has(agentName),
      ),
    },
    toolChanges: {
      added: createdTools,
      modified: updatedTools,
      removed: deletedTools,
    },
    localeChanges: {
      added: createdLocales,
      modified: updatedLocales,
      removed: deletedLocales,
    },
    profileChanges: {
      added: createdProfiles,
      modified: updatedProfiles,
      removed: deletedProfiles,
    },
    warnings: dedupe([...preview.warnings, ...stubWarnings]),
  };
}

export async function buildCoreImportApplyPlanV2(
  files: Map<string, string>,
  existingState: ExistingProjectStateV2,
  options: CoreImportPlanOptionsV2,
): Promise<CoreImportApplyPlanResultV2> {
  const importCoreLayer = isLayerEnabled(options.layers, 'core');
  const importPromptsLayer = isLayerEnabled(options.layers, 'prompts');
  const importEvalsLayer = isLayerEnabled(options.layers, 'evals');
  const prepared = prepareImportFiles(files);
  if (!prepared.success) {
    return {
      success: false,
      warnings: [],
      error: prepared.error,
    };
  }

  const importedModelPolicyResult = importCoreLayer
    ? extractImportedModelPolicyConfigs(prepared.folderResult.configFiles)
    : { success: true as const, configs: [], modelPolicyFileCount: 0 };
  if (!importedModelPolicyResult.success) {
    return {
      success: false,
      warnings: prepared.warnings,
      error: importedModelPolicyResult.error,
    };
  }

  const importedPromptResult = importPromptsLayer
    ? extractImportedPromptBundles(prepared.folderResult.promptFiles)
    : { success: true as const, prompts: new Map() };
  if (!importedPromptResult.success) {
    return {
      success: false,
      warnings: prepared.warnings,
      error: importedPromptResult.error,
    };
  }

  if (prepared.emptyProject) {
    const promptOperations = importPromptsLayer
      ? buildPromptOperations({
          existingState,
          importedPrompts: new Map(),
          deleteUnmatched: options.deleteUnmatched,
        })
      : [];
    const existingTools =
      existingState.tools ?? new Map<string, { name: string; dslContent: string }>();
    const existingMcpServers =
      existingState.mcpServers ??
      new Map<string, { name: string; config: ProjectIOMcpServerConfig }>();
    const existingLocales = buildExistingLocaleState(existingState);
    const existingProfiles = buildExistingProfileState(existingState);
    const evalOperations = importEvalsLayer
      ? buildEvalOperations({
          existing: existingState.evals,
          imported: buildEvalImportStateFromFiles(new Map()),
          deleteUnmatched: options.deleteUnmatched,
        })
      : [];
    let modelPolicyOperations = importCoreLayer
      ? buildModelPolicyOperations({
          existingState,
          importedConfigs: importedModelPolicyResult.configs,
          deleteUnmatched: options.deleteUnmatched,
        })
      : [];
    const runtimeConfigValidation = await validateRuntimeConfigWriteOperations({
      tenantId: options.tenantId,
      projectId: options.projectId,
      operations: modelPolicyOperations,
      validateRuntimeConfigForSave: options.validateRuntimeConfigForSave,
    });
    if (!runtimeConfigValidation.success) {
      return {
        success: false,
        warnings: prepared.warnings,
        error: {
          code: 'INVALID_MODEL_POLICY_CONFIG',
          message: runtimeConfigValidation.message,
        },
      };
    }
    modelPolicyOperations = runtimeConfigValidation.operations;

    const agentOperations = importCoreLayer
      ? computeApplyOperations({
          existingAgents: existingState.agents,
          importedAgents: new Map(),
        })
          .filter((operation) => options.deleteUnmatched || operation.type !== 'delete')
          .map<CoreImportAgentOperationV2>((operation) => ({
            ...operation,
            sourceHash: null,
          }))
      : [];

    const promptRefValidation = validateAgentPromptLibraryRefs({
      existingState,
      agentOperations,
      promptOperations,
    });
    if (!promptRefValidation.success) {
      return {
        success: false,
        warnings: prepared.warnings,
        error: {
          code: 'INVALID_PROMPT_LIBRARY_REFERENCE',
          message: promptRefValidation.message,
        },
      };
    }

    const toolOperations = importCoreLayer
      ? computeToolApplyOperations({
          existingTools,
          importedTools: [],
        })
          .filter((operation) => options.deleteUnmatched || operation.type !== 'delete')
          .map<CoreImportToolOperationV2>((operation) => ({
            ...operation,
            autogenerated: false,
          }))
      : [];

    const mcpServerOperations = importCoreLayer
      ? computeApplyOperations({
          existingAgents: new Map(
            [...existingMcpServers.entries()].map(([name, server]) => [
              name,
              { name, dslContent: serializeMcpServerConfigForComparison(server.config) },
            ]),
          ),
          importedAgents: new Map(),
        })
          .filter((operation) => options.deleteUnmatched || operation.type !== 'delete')
          .map<CoreImportMcpServerOperationV2>((operation) => ({
            type: operation.type,
            serverName: operation.agentName,
            config: null,
            sourceHash: null,
            sourceFile: null,
          }))
      : [];

    const localeOperations = importCoreLayer
      ? computeApplyOperations({
          existingAgents: buildLocaleApplyMap(existingLocales),
          importedAgents: new Map(),
        })
          .filter((operation) => options.deleteUnmatched || operation.type !== 'delete')
          .map<CoreImportLocaleOperationV2>((operation) => ({
            type: operation.type,
            relativePath: operation.agentName.replace(/^locales\//, ''),
            filePath: operation.agentName,
            value: null,
            description: null,
            sourceHash: null,
            sourceFile: null,
          }))
      : [];

    const profileOperations = importCoreLayer
      ? computeApplyOperations({
          existingAgents: existingProfiles,
          importedAgents: new Map(),
        })
          .filter((operation) => options.deleteUnmatched || operation.type !== 'delete')
          .map<CoreImportProfileOperationV2>((operation) => ({
            type: operation.type,
            profileName: operation.agentName,
            filePath: profileFilePath(operation.agentName),
            dslContent: null,
            sourceHash: null,
            sourceFile: null,
          }))
      : [];

    const preview = buildEmptyProjectPreview({
      formatVersion: prepared.folderResult.formatVersion,
      layers: options.layers ?? detectLayers(prepared.folderResult),
      promptOperations,
      agentOperations,
      toolOperations,
      mcpServerOperations,
      localeOperations,
      profileOperations,
      modelPolicyOperations,
      evalOperations,
      requestedEntryAgent: prepared.requestedEntryAgent,
      warnings: dedupe(prepared.warnings),
    });

    return {
      success: true,
      plan: {
        preparedFiles: prepared.preparedFiles,
        preview,
        agentOperations,
        promptOperations,
        toolOperations,
        mcpServerOperations,
        localeOperations,
        profileOperations,
        modelPolicyOperations,
        evalOperations,
        entryAgentName: null,
        warnings: dedupe(prepared.warnings),
        applied: countOperations(
          promptOperations,
          agentOperations,
          toolOperations,
          localeOperations,
          profileOperations,
          modelPolicyOperations,
          evalOperations,
        ),
      },
    };
  }

  const importedMcpServerResult = extractImportedMcpServerConfigs(
    prepared.folderResult.configFiles,
  );
  if (!importedMcpServerResult.success) {
    return {
      success: false,
      warnings: prepared.warnings,
      error: importedMcpServerResult.error,
    };
  }

  const previewResult = await importProjectV2(
    files,
    existingState,
    {
      projectId: options.projectId,
      tenantId: options.tenantId,
      userId: options.userId,
      ...(options.layers ? { layers: options.layers } : {}),
      conflictStrategy: options.conflictStrategy ?? 'replace',
      dryRun: true,
    },
    createNoopImportDeps(),
  );

  if (!previewResult.success) {
    return {
      success: false,
      warnings: previewResult.warnings,
      error: previewResult.error ?? {
        code: 'VALIDATION_FAILED',
        message: 'Import validation failed',
      },
      preview: previewResult.preview,
    };
  }

  const baseWarnings = dedupe([...prepared.warnings, ...previewResult.warnings]);
  const basePreview = buildUnsupportedDirectApplyIssue({
    ...previewResult.preview,
    warnings: dedupe([...previewResult.preview.warnings, ...baseWarnings]),
  });

  if (basePreview.hasBlockingIssues) {
    return {
      success: true,
      plan: {
        preparedFiles: prepared.preparedFiles,
        preview: basePreview,
        agentOperations: [],
        promptOperations: [],
        toolOperations: [],
        mcpServerOperations: [],
        localeOperations: [],
        profileOperations: [],
        modelPolicyOperations: [],
        evalOperations: [],
        entryAgentName: basePreview.entryAgentResolution.resolved,
        warnings: baseWarnings,
        applied: countOperations([], [], [], [], [], [], []),
      },
    };
  }

  const agentIdentity = resolveImportedAgentIdentities(
    prepared.folderResult.agentFiles,
    prepared.folderResult.manifestV2 ?? prepared.folderResult.manifest,
  );
  const importedAgents = new Map<
    string,
    {
      name: string;
      dslContent: string;
      description: string | null;
      systemPromptLibraryRef?: AgentPromptLibraryRefSnapshot | null;
    }
  >();
  for (const [name, agent] of agentIdentity.agents) {
    importedAgents.set(name, {
      name,
      dslContent: agent.dslContent,
      description: agent.description,
      systemPromptLibraryRef: agent.systemPromptLibraryRef ?? null,
    });
  }

  const agentOperations = importCoreLayer
    ? computeApplyOperations({
        existingAgents: existingState.agents,
        importedAgents,
      })
        .filter((operation) => options.deleteUnmatched || operation.type !== 'delete')
        .map<CoreImportAgentOperationV2>((operation) => ({
          ...operation,
          sourceHash: computeProjectAgentDraftSourceHash({
            recordName: operation.agentName,
            dslContent: operation.dslContent,
            systemPromptLibraryRef: operation.systemPromptLibraryRef ?? null,
          }),
        }))
    : [];
  const promptOperations = importPromptsLayer
    ? buildPromptOperations({
        existingState,
        importedPrompts: importedPromptResult.prompts,
        deleteUnmatched: options.deleteUnmatched,
      })
    : [];

  const promptRefValidation = validateAgentPromptLibraryRefs({
    existingState,
    agentOperations,
    promptOperations,
  });
  if (!promptRefValidation.success) {
    return {
      success: false,
      warnings: baseWarnings,
      error: {
        code: 'INVALID_PROMPT_LIBRARY_REFERENCE',
        message: promptRefValidation.message,
      },
      preview: basePreview,
    };
  }

  const toolExtraction = extractToolsFromFiles(prepared.folderResult.toolFiles);
  const existingTools =
    existingState.tools ?? new Map<string, { name: string; dslContent: string }>();
  const existingMcpServers =
    existingState.mcpServers ??
    new Map<string, { name: string; config: ProjectIOMcpServerConfig }>();
  const existingLocales = buildExistingLocaleState(existingState);
  const existingProfiles = buildExistingProfileState(existingState);
  const importedLocaleDescriptions = readCoreImportSnapshotLocaleDescriptions(
    prepared.preparedFiles,
  );
  const importedLocales = buildImportedLocaleState(
    prepared.folderResult.localeFiles,
    importedLocaleDescriptions,
  );
  const importedProfiles = buildImportedProfileState(prepared.folderResult.profileFiles);
  let importedEvals: ReturnType<typeof buildEvalImportStateFromFiles>;
  try {
    importedEvals = importEvalsLayer
      ? buildEvalImportStateFromFiles(prepared.folderResult.evalFiles)
      : buildEvalImportStateFromFiles(new Map());
  } catch (error) {
    return {
      success: false,
      warnings: baseWarnings,
      error: {
        code: 'INVALID_EVAL_IMPORT',
        message: error instanceof Error ? error.message : 'Invalid eval import data',
      },
      preview: basePreview,
    };
  }
  const evalOperations = importEvalsLayer
    ? buildEvalOperations({
        existing: existingState.evals,
        imported: importedEvals,
        deleteUnmatched: options.deleteUnmatched,
      })
    : [];
  let modelPolicyOperations = importCoreLayer
    ? buildModelPolicyOperations({
        existingState,
        importedConfigs: importedModelPolicyResult.configs,
        deleteUnmatched: options.deleteUnmatched,
      })
    : [];
  const runtimeConfigValidation = await validateRuntimeConfigWriteOperations({
    tenantId: options.tenantId,
    projectId: options.projectId,
    operations: modelPolicyOperations,
    validateRuntimeConfigForSave: options.validateRuntimeConfigForSave,
  });
  if (!runtimeConfigValidation.success) {
    return {
      success: false,
      warnings: baseWarnings,
      error: {
        code: 'INVALID_MODEL_POLICY_CONFIG',
        message: runtimeConfigValidation.message,
      },
      preview: basePreview,
    };
  }
  modelPolicyOperations = runtimeConfigValidation.operations;

  const declaredToolResult = importCoreLayer
    ? extractToolSignaturesFromAgents(prepared.folderResult.agentFiles)
    : { tools: [], errors: [] };
  const declaredToolNames = new Set(declaredToolResult.tools.map((tool) => tool.name));
  const declaredToolWarnings = dedupe(
    declaredToolResult.errors.map(
      ({ file, message }) =>
        `W_DECLARED_TOOL_PARSE: Skipped declared tool extraction for "${file}" - ${message}`,
    ),
  );

  const rawToolOperations = importCoreLayer
    ? computeToolApplyOperations({
        existingTools,
        importedTools: toolExtraction.tools,
      })
    : [];

  const rawMcpServerOperations = importCoreLayer
    ? computeApplyOperations({
        existingAgents: new Map(
          [...existingMcpServers.entries()].map(([name, server]) => [
            name,
            {
              name,
              dslContent: serializeMcpServerConfigForComparison(server.config),
              description: server.config.description,
            },
          ]),
        ),
        importedAgents: new Map(
          [...importedMcpServerResult.mcpServers.entries()].map(([name, server]) => [
            name,
            {
              name,
              dslContent: server.sourceContent,
              description: server.config.description,
            },
          ]),
        ),
      })
    : [];

  const toolOperations: CoreImportToolOperationV2[] = importCoreLayer
    ? rawToolOperations
        .filter(
          (operation) =>
            (options.deleteUnmatched || operation.type !== 'delete') &&
            (toolExtraction.incompleteFiles.length === 0 || operation.type !== 'delete') &&
            !(operation.type === 'delete' && declaredToolNames.has(operation.toolName)),
        )
        .map((operation) => ({
          ...operation,
          autogenerated: false,
        }))
    : [];

  const mcpServerOperations: CoreImportMcpServerOperationV2[] = importCoreLayer
    ? rawMcpServerOperations
        .filter((operation) => options.deleteUnmatched || operation.type !== 'delete')
        .map((operation) => {
          if (operation.type === 'delete') {
            return {
              type: 'delete' as const,
              serverName: operation.agentName,
              config: null,
              sourceHash: null,
              sourceFile: null,
            };
          }

          const importedServer = importedMcpServerResult.mcpServers.get(operation.agentName);
          if (!importedServer) {
            throw new Error(`Missing imported MCP server config for ${operation.agentName}`);
          }

          return {
            type: operation.type,
            serverName: operation.agentName,
            config: importedServer.config,
            sourceHash: computeSourceHash(importedServer.sourceContent),
            sourceFile: importedServer.sourceFile,
          };
        })
    : [];

  const localeOperations = importCoreLayer
    ? computeApplyOperations({
        existingAgents: buildLocaleApplyMap(existingLocales),
        importedAgents: buildLocaleApplyMap(importedLocales),
      })
        .filter((operation) => options.deleteUnmatched || operation.type !== 'delete')
        .map<CoreImportLocaleOperationV2>((operation) => ({
          type: operation.type,
          relativePath: operation.agentName.replace(/^locales\//, ''),
          filePath: operation.agentName,
          value: operation.dslContent,
          description: operation.description,
          sourceHash: operation.dslContent ? computeSourceHash(operation.dslContent) : null,
          sourceFile: operation.type === 'delete' ? null : operation.agentName,
        }))
    : [];

  const profileOperations = importCoreLayer
    ? computeApplyOperations({
        existingAgents: existingProfiles,
        importedAgents: importedProfiles,
      })
        .filter((operation) => options.deleteUnmatched || operation.type !== 'delete')
        .map<CoreImportProfileOperationV2>((operation) => {
          const sourceFile =
            operation.type === 'delete'
              ? null
              : (importedProfiles.get(operation.agentName)?.sourceFile ??
                profileFilePath(operation.agentName));

          return {
            type: operation.type,
            profileName: operation.agentName,
            filePath: sourceFile ?? profileFilePath(operation.agentName),
            dslContent: operation.dslContent,
            sourceHash: operation.dslContent ? computeSourceHash(operation.dslContent) : null,
            sourceFile,
          };
        })
    : [];

  const deletedToolNames = new Set(
    toolOperations
      .filter((operation) => operation.type === 'delete')
      .map((operation) => operation.toolName),
  );
  const retainedExistingToolNames = [...existingTools.keys()].filter(
    (toolName) => !deletedToolNames.has(toolName),
  );
  const coveredToolNames = new Set<string>([
    ...toolExtraction.tools.map((tool) => tool.name),
    ...retainedExistingToolNames,
  ]);

  const stubWarnings: string[] = [];
  for (const declaredTool of importCoreLayer ? declaredToolResult.tools : []) {
    if (coveredToolNames.has(declaredTool.name)) {
      continue;
    }

    const dslContent = synthesizeToolDsl(declaredTool);
    toolOperations.push({
      type: 'create',
      toolName: declaredTool.name,
      toolType: 'http',
      dslContent,
      description: declaredTool.description,
      sourceHash: computeSourceHash(dslContent),
      sourceFile: `auto:${declaredTool.sourceAgent}`,
      autogenerated: true,
    });
    coveredToolNames.add(declaredTool.name);
    stubWarnings.push(
      `W_TOOL_STUB: Auto-created stub for "${declaredTool.name}" — configure endpoint and auth in Tool Library`,
    );
  }

  const normalizedToolOperations = await normalizeToolWriteOperationsForPersistence({
    tenantId: options.tenantId,
    projectId: options.projectId,
    operations: toolOperations,
    validateToolBindingForSave: options.validateToolBindingForSave,
  });
  if (!normalizedToolOperations.success) {
    const preview = adjustPreviewForPlan(
      {
        ...basePreview,
        warnings: dedupe([...basePreview.warnings, ...declaredToolWarnings, ...stubWarnings]),
      },
      promptOperations,
      agentOperations,
      toolOperations,
      mcpServerOperations,
      localeOperations,
      profileOperations,
      modelPolicyOperations,
      evalOperations,
      stubWarnings,
    );

    return {
      success: false,
      warnings: dedupe([...baseWarnings, ...declaredToolWarnings, ...stubWarnings]),
      error: {
        code: 'INVALID_TOOL_IMPORT',
        message: normalizedToolOperations.message,
      },
      preview,
    };
  }

  const preview = adjustPreviewForPlan(
    {
      ...basePreview,
      warnings: dedupe([...basePreview.warnings, ...declaredToolWarnings]),
    },
    promptOperations,
    agentOperations,
    normalizedToolOperations.operations,
    mcpServerOperations,
    localeOperations,
    profileOperations,
    modelPolicyOperations,
    evalOperations,
    stubWarnings,
  );
  const warnings = dedupe([...baseWarnings, ...declaredToolWarnings, ...stubWarnings]);

  return {
    success: true,
    plan: {
      preparedFiles: prepared.preparedFiles,
      preview,
      agentOperations,
      promptOperations,
      toolOperations: normalizedToolOperations.operations,
      mcpServerOperations,
      localeOperations,
      profileOperations,
      modelPolicyOperations,
      evalOperations,
      entryAgentName: preview.entryAgentResolution.resolved,
      warnings,
      applied: countOperations(
        promptOperations,
        agentOperations,
        toolOperations,
        localeOperations,
        profileOperations,
        modelPolicyOperations,
        evalOperations,
      ),
    },
  };
}

