function stripImportConfigInternalFields(data: Record<string, unknown>): Record<string, unknown> {
  return Object.fromEntries(Object.entries(data).filter(([key]) => !INTERNAL_CONFIG_KEYS.has(key)));
}

function parseJsonConfigFile(
  filePath: string,
  content: string,
): { success: true; data: Record<string, unknown> } | { success: false; message: string } {
  try {
    const parsed = JSON.parse(content) as unknown;
    if (!parsed || typeof parsed !== 'object' || Array.isArray(parsed)) {
      return {
        success: false,
        message: `Expected ${filePath} to contain a JSON object`,
      };
    }
    return {
      success: true,
      data: stripImportConfigInternalFields(parsed as Record<string, unknown>),
    };
  } catch (error) {
    return {
      success: false,
      message: `Invalid JSON in ${filePath}: ${error instanceof Error ? error.message : String(error)}`,
    };
  }
}

function stableConfigStringify(value: unknown): string {
  if (value === null || typeof value !== 'object') {
    return JSON.stringify(value);
  }
  if (Array.isArray(value)) {
    return `[${value.map((entry) => stableConfigStringify(entry)).join(',')}]`;
  }
  const record = value as Record<string, unknown>;
  return `{${Object.keys(record)
    .sort()
    .map((key) => `${JSON.stringify(key)}:${stableConfigStringify(record[key])}`)
    .join(',')}}`;
}

function operationTierOverridesFingerprint(entry: ImportedModelPolicyConfigEntry | undefined) {
  if (!entry || !Object.prototype.hasOwnProperty.call(entry.data, 'operationTierOverrides')) {
    return null;
  }
  return stableConfigStringify(entry.data.operationTierOverrides ?? {});
}

function canonicalizeRuntimeOnlyOperationTierOverrides(
  configs: ImportedModelPolicyConfigEntry[],
): void {
  const runtimeConfig = configs.find((config) => config.configType === 'runtime');
  const llmConfig = configs.find((config) => config.configType === 'llm');
  if (!runtimeConfig || llmConfig) {
    return;
  }
  if (!Object.prototype.hasOwnProperty.call(runtimeConfig.data, 'operationTierOverrides')) {
    return;
  }

  const operationTierOverrides = runtimeConfig.data.operationTierOverrides ?? {};
  delete runtimeConfig.data.operationTierOverrides;
  runtimeConfig.sourceHash = computeSourceHash(stableConfigStringify(runtimeConfig.data));
  configs.push({
    configType: 'llm',
    data: { operationTierOverrides },
    sourceFile: runtimeConfig.sourceFile,
    sourceHash: computeSourceHash(stableConfigStringify({ operationTierOverrides })),
  });
}

function computeModelPolicyOperationType(
  existing: Record<string, unknown> | undefined | null,
  imported: Record<string, unknown>,
): 'upsert' | null {
  if (!existing) {
    return 'upsert';
  }
  return stableConfigStringify(stripImportConfigInternalFields(existing)) ===
    stableConfigStringify(stripImportConfigInternalFields(imported))
    ? null
    : 'upsert';
}

function extractImportedModelPolicyConfigs(configFiles: Map<string, string>):
  | {
      success: true;
      configs: ImportedModelPolicyConfigEntry[];
      modelPolicyFileCount: number;
    }
  | {
      success: false;
      error: { code: string; message: string };
    } {
  const configs: ImportedModelPolicyConfigEntry[] = [];
  let modelPolicyFileCount = 0;
  const seenProjectConfigs = new Set<string>();
  const seenAgentConfigs = new Set<string>();

  for (const [filePath, content] of configFiles) {
    let configType: CoreImportModelPolicyConfigType | null = null;
    let modelConfigNameFromPath: string | undefined;
    let agentNameFromPath: string | undefined;

    if (filePath === RUNTIME_CONFIG_FILE) {
      configType = 'runtime';
    } else if (filePath === LLM_CONFIG_FILE) {
      configType = 'llm';
    } else {
      const projectModelMatch = filePath.match(PROJECT_MODEL_CONFIG_PATTERN);
      if (projectModelMatch) {
        configType = 'project_model';
        modelConfigNameFromPath = projectModelMatch[1];
      } else {
        const match = filePath.match(AGENT_MODEL_CONFIG_PATTERN);
        if (match) {
          configType = 'agent_model';
          agentNameFromPath = match[1];
        }
      }
    }

    if (!configType) {
      continue;
    }

    modelPolicyFileCount++;
    const parsed = parseJsonConfigFile(filePath, content);
    if (!parsed.success) {
      return {
        success: false,
        error: {
          code: 'INVALID_MODEL_POLICY_CONFIG',
          message: parsed.message,
        },
      };
    }

    const data = parsed.data;
    if (
      (configType === 'runtime' || configType === 'llm') &&
      Object.prototype.hasOwnProperty.call(data, 'operationTierOverrides')
    ) {
      const validation = normalizeOperationTierOverrides(data.operationTierOverrides);
      if (!validation.ok) {
        return {
          success: false,
          error: {
            code: 'INVALID_MODEL_POLICY_CONFIG',
            message: `${filePath}: ${formatOperationTierOverrideError(validation)}`,
          },
        };
      }
      data.operationTierOverrides = validation.overrides;
    }

    if (configType === 'project_model') {
      const modelConfigName =
        typeof data.name === 'string' && data.name.trim().length > 0
          ? data.name.trim()
          : modelConfigNameFromPath;
      if (!modelConfigName) {
        return {
          success: false,
          error: {
            code: 'INVALID_MODEL_POLICY_CONFIG',
            message: `Project model config ${filePath} is missing name`,
          },
        };
      }
      if (seenProjectConfigs.has(modelConfigName)) {
        return {
          success: false,
          error: {
            code: 'DUPLICATE_MODEL_POLICY_CONFIG',
            message: `Duplicate project model config for "${modelConfigName}"`,
          },
        };
      }
      const tier = data.tier;
      if (typeof tier !== 'string' || !isModelRoutingTier(tier)) {
        return {
          success: false,
          error: {
            code: 'INVALID_MODEL_POLICY_CONFIG',
            message: `Project model config ${filePath} has invalid tier "${String(tier)}"`,
          },
        };
      }
      seenProjectConfigs.add(modelConfigName);
      data.name = modelConfigName;
      delete data.tenantModelId;
      delete data.credentialId;
      delete data.authProfileId;
      configs.push({
        configType,
        modelConfigName,
        data,
        sourceFile: filePath,
        sourceHash: computeSourceHash(stableConfigStringify(data)),
      });
      continue;
    }

    const agentName =
      configType === 'agent_model'
        ? typeof data.agentName === 'string' && data.agentName.trim().length > 0
          ? data.agentName.trim()
          : agentNameFromPath
        : undefined;

    if (configType === 'agent_model') {
      if (!agentName) {
        return {
          success: false,
          error: {
            code: 'INVALID_MODEL_POLICY_CONFIG',
            message: `Agent model config ${filePath} is missing agentName`,
          },
        };
      }
      if (seenAgentConfigs.has(agentName)) {
        return {
          success: false,
          error: {
            code: 'DUPLICATE_MODEL_POLICY_CONFIG',
            message: `Duplicate agent model config for "${agentName}"`,
          },
        };
      }
      seenAgentConfigs.add(agentName);
      data.agentName = agentName;
    }

    configs.push({
      configType,
      ...(agentName ? { agentName } : {}),
      data,
      sourceFile: filePath,
      sourceHash: computeSourceHash(stableConfigStringify(data)),
    });
  }

  const runtimeConfig = configs.find((config) => config.configType === 'runtime');
  const llmConfig = configs.find((config) => config.configType === 'llm');
  const runtimeOverrides = operationTierOverridesFingerprint(runtimeConfig);
  const llmOverrides = operationTierOverridesFingerprint(llmConfig);
  if (runtimeOverrides !== null && llmOverrides !== null && runtimeOverrides !== llmOverrides) {
    return {
      success: false,
      error: {
        code: 'INVALID_MODEL_POLICY_CONFIG',
        message:
          'config/runtime-config.json and config/llm-config.json define conflicting operationTierOverrides; keep operation-tier overrides in config/llm-config.json or make both files match',
      },
    };
  }
  canonicalizeRuntimeOnlyOperationTierOverrides(configs);

  return { success: true, configs, modelPolicyFileCount };
}

