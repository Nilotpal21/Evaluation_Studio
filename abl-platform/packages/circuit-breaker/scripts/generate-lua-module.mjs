import { promises as fs } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const packageRoot = resolve(scriptDir, '..');
const luaDir = resolve(packageRoot, 'src/lua');
const outputPath = resolve(packageRoot, 'src/generated-lua.ts');

const scripts = [
  ['CHECK_STATE_LUA', 'check-state.lua'],
  ['FORCE_RESET_LUA', 'force-reset.lua'],
  ['RECORD_FAILURE_LUA', 'record-failure.lua'],
  ['RECORD_SUCCESS_LUA', 'record-success.lua'],
];

const entries = await Promise.all(
  scripts.map(async ([constantName, filename]) => {
    const body = await fs.readFile(resolve(luaDir, filename), 'utf8');
    return `export const ${constantName} =\n  ${JSON.stringify(body)};`;
  }),
);

const source = `/**
 * Generated by scripts/generate-lua-module.mjs.
 *
 * Do not edit directly; update src/lua/*.lua and run the package build.
 */

${entries.join('\n\n')}
`;

await fs.writeFile(outputPath, source);
