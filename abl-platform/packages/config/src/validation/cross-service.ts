export interface ServiceConfig {
  name: string;
  server?: { port?: number; host?: string; apiUrl?: string };
  jwt?: { secret?: string };
  database?: { url?: string };
  redis?: { url?: string; host?: string; port?: number };
}

export interface CrossServiceIssue {
  level: 'error' | 'warning';
  field: string;
  message: string;
  services: string[];
}

/**
 * Validate consistency across multiple service configs.
 * Catches URL mismatches, shared secret inconsistencies, and DB cluster divergence.
 */
export function validateCrossServiceConfig(configs: ServiceConfig[]): CrossServiceIssue[] {
  const issues: CrossServiceIssue[] = [];

  // Rule 1: JWT secret must be identical across all services
  const jwtSecrets = new Map<string, string[]>();
  for (const cfg of configs) {
    if (cfg.jwt?.secret) {
      const secret = cfg.jwt.secret;
      if (!jwtSecrets.has(secret)) jwtSecrets.set(secret, []);
      jwtSecrets.get(secret)!.push(cfg.name);
    }
  }
  if (jwtSecrets.size > 1) {
    const services = configs.filter((c) => c.jwt?.secret).map((c) => c.name);
    issues.push({
      level: 'error',
      field: 'jwt.secret',
      message: `JWT secret differs across services. Tokens generated by one service will not validate on another.`,
      services,
    });
  }

  // Rule 2: MongoDB URL must point to same cluster
  const dbHosts = new Map<string, string[]>();
  for (const cfg of configs) {
    if (cfg.database?.url) {
      try {
        const host = new URL(cfg.database.url).hostname;
        if (!dbHosts.has(host)) dbHosts.set(host, []);
        dbHosts.get(host)!.push(cfg.name);
      } catch {
        issues.push({
          level: 'warning',
          field: 'database.url',
          message: `Service "${cfg.name}" has unparseable database URL`,
          services: [cfg.name],
        });
      }
    }
  }
  if (dbHosts.size > 1) {
    const services = configs.filter((c) => c.database?.url).map((c) => c.name);
    issues.push({
      level: 'error',
      field: 'database.url',
      message: `MongoDB host differs across services: ${[...dbHosts.keys()].join(', ')}. Services must use the same cluster.`,
      services,
    });
  }

  // Rule 3: Redis must point to same instance
  const redisHosts = new Map<string, string[]>();
  for (const cfg of configs) {
    let redisHost: string | undefined = cfg.redis?.host;
    if (!redisHost && cfg.redis?.url) {
      try {
        redisHost = new URL(cfg.redis.url).hostname;
      } catch {
        issues.push({
          level: 'warning',
          field: 'redis.url',
          message: `Service "${cfg.name}" has unparseable Redis URL`,
          services: [cfg.name],
        });
      }
    }
    if (redisHost) {
      if (!redisHosts.has(redisHost)) redisHosts.set(redisHost, []);
      redisHosts.get(redisHost)!.push(cfg.name);
    }
  }
  if (redisHosts.size > 1) {
    const services = configs.filter((c) => c.redis?.host || c.redis?.url).map((c) => c.name);
    issues.push({
      level: 'error',
      field: 'redis',
      message: `Redis host differs across services: ${[...redisHosts.keys()].join(', ')}.`,
      services,
    });
  }

  return issues;
}
