function renderFrontmatter(title: string, description: string, order: number): string {
  return [
    '---',
    `title: '${title}'`,
    "section: 'abl-reference'",
    `order: ${order}`,
    `description: '${description}'`,
    '---',
    '',
  ].join('\n');
}

function stripLeadingH1(markdown: string): string {
  const lines = markdown.split('\n');

  if (lines[0]?.startsWith('# ')) {
    lines.shift();
  }

  while (lines[0]?.trim() === '') {
    lines.shift();
  }

  return lines.join('\n').trimStart();
}

async function renderCanonicalDocMirror(config: CanonicalDocMirrorConfig): Promise<string> {
  const absoluteSourcePath = path.join(REPO_ROOT, config.sourcePath);
  const markdown = await fs.readFile(absoluteSourcePath, 'utf8');
  const body = stripLeadingH1(markdown);

  return [
    renderFrontmatter(config.title, config.description, config.order).trimEnd(),
    '',
    `> This page is generated from \`${config.sourcePath}\` by \`pnpm abl:docs:generate\`.`,
    '> Do not edit it directly; update the canonical source document instead.',
    '',
    body,
    '',
  ].join('\n');
}

function renderGeneratedFactsBody(registry: ABLContractRegistry): string {
  const lines: string[] = [];

  lines.push('> This page is generated by `pnpm abl:docs:generate`.');
  lines.push('> Do not edit it directly; update the contract registry instead.');
  lines.push('');
  lines.push('## Overview');
  lines.push('');
  lines.push(
    'This generated reference captures code-backed ABL facts from the compiler layer. The current foundation slice focuses on registry-backed public surfaces, built-in functions, lifecycle events, coordination semantics, and system variable behavior.',
  );
  lines.push('');
  lines.push('## Stability Tiers');
  lines.push('');
  lines.push('| Tier | Summary | Guidance |');
  lines.push('| ---- | ------- | -------- |');
  for (const tier of registry.stabilityTiers) {
    lines.push(`| \`${tier.tier}\` | ${tier.summary} | ${tier.guidance} |`);
  }
  lines.push('');
  lines.push('## Public Surfaces');
  lines.push('');
  lines.push('| Surface | Scope | Stability | Support | Summary |');
  lines.push('| ------- | ----- | --------- | ------- | ------- |');
  for (const construct of registry.constructs) {
    lines.push(
      `| \`${construct.id}\` | ${construct.scope} | \`${construct.stability}\` | \`${construct.support}\` | ${construct.summary} |`,
    );
  }
  lines.push('');
  lines.push(`## Built-in Functions (${registry.builtInFunctions.count})`);
  lines.push('');
  lines.push('| Category | Count | Functions |');
  lines.push('| -------- | ----- | --------- |');
  for (const category of registry.builtInFunctions.categories) {
    lines.push(
      `| ${category.name} | ${category.functions.length} | ${category.functions
        .map((name) => `\`${name}\``)
        .join(', ')} |`,
    );
  }
  lines.push('');
  lines.push('### Function Purity');
  lines.push('');
  lines.push('| Name | Category | Pure? |');
  lines.push('| ---- | -------- | ----- |');
  for (const fn of registry.builtInFunctions.functions) {
    lines.push(`| \`${fn.name}\` | ${fn.category} | ${fn.pure ? 'yes' : 'no'} |`);
  }
  lines.push('');
  lines.push('## Lifecycle / Recall Events');
  lines.push('');
  lines.push('| Canonical Event | Legacy Aliases | Stability | Description |');
  lines.push('| --------------- | -------------- | --------- | ----------- |');
  for (const event of registry.lifecycleEvents) {
    lines.push(
      `| \`${event.canonical}\` | ${
        event.legacyAliases.length > 0
          ? event.legacyAliases.map((alias) => `\`${alias}\``).join(', ')
          : '-'
      } | \`${event.stability}\` | ${event.description} |`,
    );
  }
  lines.push('');
  lines.push('## Handoff History Strategies');
  lines.push('');
  lines.push('| Strategy | Syntax | Stability | Support | Description |');
  lines.push('| -------- | ------ | --------- | ------- | ----------- |');
  for (const strategy of registry.historyStrategies) {
    lines.push(
      `| \`${strategy.name}\` | \`${strategy.syntax}\` | \`${strategy.stability}\` | \`${strategy.support}\` | ${strategy.description} |`,
    );
  }
  lines.push('');
  lines.push('## Coordination Actions');
  lines.push('');
  lines.push('| Surface | Syntax | Stability | Support | Semantics |');
  lines.push('| ------- | ------ | --------- | ------- | --------- |');
  for (const action of registry.coordinationActions) {
    lines.push(
      `| \`${action.surface}\` | \`${action.syntax}\` | \`${action.stability}\` | \`${action.support}\` | ${action.semantics} |`,
    );
  }
  lines.push('');
  lines.push('## System Variables');
  lines.push('');
  lines.push('| Name | Surfaces | Stability | Support | Description |');
  lines.push('| ---- | -------- | --------- | ------- | ----------- |');
  for (const variable of registry.systemVariables) {
    lines.push(
      `| \`${variable.name}\` | ${variable.surfaces
        .map((surface) => `\`${surface}\``)
        .join(
          ', ',
        )} | \`${variable.stability}\` | \`${variable.support}\` | ${variable.description} |`,
    );
  }
  lines.push('');
  lines.push('## Compatibility Notes');
  lines.push('');
  lines.push('| ID | Category | Summary | Guidance |');
  lines.push('| -- | -------- | ------- | -------- |');
  for (const note of registry.compatibilityNotes) {
    lines.push(`| \`${note.id}\` | \`${note.category}\` | ${note.summary} | ${note.guidance} |`);
  }
  lines.push('');
  lines.push('## Source Files');
  lines.push('');
  for (const sourceFile of registry.sourceFiles) {
    lines.push(`- \`${sourceFile}\``);
  }
  lines.push('');

  return lines.join('\n');
}

function renderReferenceMarkdown(registry: ABLContractRegistry): string {
  return ['# ABL Contract Facts', '', renderGeneratedFactsBody(registry)].join('\n');
}

function renderReferenceMdx(registry: ABLContractRegistry): string {
  return (
    renderFrontmatter(
      'ABL Contract Facts',
      'Generated factual reference for built-in functions, lifecycle events, coordination actions, and system variables.',
      95,
    ) + renderGeneratedFactsBody(registry)
  );
}

export async function getGeneratedArtifacts(): Promise<GeneratedArtifact[]> {
  const registry = getAblContractRegistry();
  const mirrorArtifacts = await Promise.all(
    CANONICAL_DOC_MIRRORS.map(async (mirrorConfig) => ({
      relativePath: mirrorConfig.relativePath,
      content: `${await renderCanonicalDocMirror(mirrorConfig)}\n`,
    })),
  );

  const rawArtifacts: GeneratedArtifact[] = [
    {
      relativePath: 'docs/reference/generated/abl-contract.json',
      content: `${JSON.stringify(registry, null, 2)}\n`,
    },
    {
      relativePath: 'docs/reference/generated/abl-contract-facts.md',
      content: `${renderReferenceMarkdown(registry)}\n`,
    },
    {
      relativePath: 'apps/docs-internal/content/abl-reference/contract-facts.mdx',
      content: `${renderReferenceMdx(registry)}\n`,
    },
    {
      relativePath: 'apps/studio/content/abl-reference/contract-facts.mdx',
      content: `${renderReferenceMdx(registry)}\n`,
    },
    ...mirrorArtifacts,
  ];

  // L2 knowledge cards auto-generated from docs-internal MDX
  const docsContentDir = path.join(REPO_ROOT, 'apps/docs-internal/content');
  const generatedCards = await generateAllCards(docsContentDir);
  for (const card of generatedCards) {
    rawArtifacts.push({
      relativePath: `packages/arch-ai/src/knowledge/cards/generated/${card.fileName}`,
      content: card.tsSource,
    });
  }

  // L2 platform knowledge cards (channels, deployments, auth, etc.)
  const platformCards = await generateAllCards(docsContentDir, PLATFORM_CARD_MAPPINGS);
  for (const card of platformCards) {
    rawArtifacts.push({
      relativePath: `packages/arch-ai/src/knowledge/cards/platform/${card.fileName}`,
      content: card.tsSource,
    });
  }

  // L3 BM25 index from docs-internal MDX
  const l3Index = await generateL3Index(docsContentDir);
  rawArtifacts.push({
    relativePath: 'packages/arch-ai/src/knowledge/l3-index.json',
    content: JSON.stringify(l3Index, null, 2) + '\n',
  });

  return Promise.all(
    rawArtifacts.map(async (artifact) => {
      const absolutePath = path.join(REPO_ROOT, artifact.relativePath);
      const prettierConfig = (await prettier.resolveConfig(absolutePath)) ?? {};

      return {
        ...artifact,
        content: await prettier.format(artifact.content, {
          ...prettierConfig,
          filepath: absolutePath,
        }),
      };
    }),
  );
}

export async function writeGeneratedArtifacts(): Promise<string[]> {
  const artifacts = await getGeneratedArtifacts();
  const writtenPaths: string[] = [];

  for (const artifact of artifacts) {
    const absolutePath = path.join(REPO_ROOT, artifact.relativePath);
    await fs.mkdir(path.dirname(absolutePath), { recursive: true });
    await fs.writeFile(absolutePath, artifact.content, 'utf8');
    writtenPaths.push(absolutePath);
  }

  return writtenPaths;
}

export async function getOutdatedArtifacts(): Promise<string[]> {
  const artifacts = await getGeneratedArtifacts();
  const outdated: string[] = [];

  for (const artifact of artifacts) {
    const absolutePath = path.join(REPO_ROOT, artifact.relativePath);
    let current = '';

    try {
      current = await fs.readFile(absolutePath, 'utf8');
    } catch {
      outdated.push(artifact.relativePath);
      continue;
    }

    if (current !== artifact.content) {
      outdated.push(artifact.relativePath);
    }
  }

  return outdated;
}
